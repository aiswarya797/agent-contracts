def test_paths_from_commands(commands: list[dict[str, Any]], candidate_tests: set[str]) -> set[str]:
    found: set[str] = set()
    for command in commands:
        text = str(command.get("command", ""))
        for path in candidate_tests:
            if path in text:
                found.add(path)
    return found


def file_size_for_trial(repo: Path, path: str) -> int:
    try:
        return (repo / path).stat().st_size
    except OSError:
        return 0


def agent_final_status_failed(status: str) -> bool:
    normalized = status.strip().lower()
    if not normalized:
        return False
    return normalized in {"failed", "failure", "error", "timeout", "invalid-json"}


def append_agent_trace(trace: list[dict[str, Any]], operation: str, **fields: Any) -> None:
    step: dict[str, Any] = {"rank": len(trace) + 1, "operation": operation}
    step.update(fields)
    trace.append(step)


def build_agent_trial_trace(
    strategy: str,
    selection: dict[str, Any],
    agent_result: dict[str, Any],
    files_read: list[str],
    files_edited: list[str],
    test_commands: list[dict[str, Any]],
    failure_reasons: list[str],
) -> list[dict[str, Any]]:
    trace: list[dict[str, Any]] = []
    append_agent_trace(
        trace,
        "strategy_context_selected",
        strategy=strategy,
        file_count=len(selection.get("included_files", [])),
        selected_bytes=selection.get("selected_bytes", 0),
    )
    for step in selection.get("trace", []):
        copied = dict(step)
        copied["rank"] = len(trace) + 1
        copied.setdefault("operation", copied.get("tool", "strategy_context_trace"))
        append_agent_trace(trace, str(copied.pop("operation")), **{key: value for key, value in copied.items() if key != "rank"})
    append_agent_trace(trace, "agent_command_started", mode=agent_result.get("mode", "mock"), strategy=strategy)
    for path in files_read:
        append_agent_trace(trace, "file_read", path=path)
    for path in files_edited:
        append_agent_trace(trace, "file_edit", path=path)
    for command in test_commands:
        append_agent_trace(
            trace,
            "test_command",
            command=command.get("command"),
            exit_code=command.get("exit_code"),
            passed=command_passed(command),
        )
    append_agent_trace(trace, "scoring", agent_success=not failure_reasons, failure_reasons=failure_reasons)
    return trace


def score_agent_trial_run(
    repo: Path,
    row: dict[str, Any],
    strategy: str,
    selection: dict[str, Any],
    agent_result: dict[str, Any],
    *,
    mode: str,
    run_index: int,
    git_diff: str,
) -> dict[str, Any]:
    normalized = normalize_agent_result(agent_result)
    normalized["mode"] = mode
    preloaded_paths = [normalize_context_path(path) for path in selection.get("included_files", [])]
    files_read_paths = unique_ordered(preloaded_paths + normalized["files_read"])
    diff_edited_paths = git_changed_files(repo)
    files_edited_paths = diff_edited_paths or normalized["files_edited"]
    commands = normalized["commands_run"]
    test_commands = [command for command in commands if is_test_command(command)]
    test_command_passes = [command_passed(command) for command in test_commands]
    tests_passed = None if not test_commands or any(item is None for item in test_command_passes) else all(bool(item) for item in test_command_passes)

    target_files = set(row["target_files"])
    relevant_tests = set(row["relevant_tests"])
    required_context = set(row.get("required_context", []))
    allowed_context = set(row.get("allowed_context", []))
    misleading_files = set(row["misleading_files"])
    forbidden_paths = set(row.get("forbidden_paths", []))
    known_scoring_paths = target_files | relevant_tests | required_context | allowed_context | misleading_files | forbidden_paths
    read_set = set(files_read_paths)
    edited_set = set(files_edited_paths)
    target_files_read = sorted(target_files & read_set)
    target_files_edited = sorted(target_files & edited_set)
    relevant_tests_read = sorted(relevant_tests & read_set)
    required_context_read = sorted(required_context & read_set)
    misleading_files_read = sorted(misleading_files & read_set)
    misleading_files_edited = sorted(misleading_files & edited_set)
    irrelevant_files_read_paths = sorted(path for path in read_set if path not in known_scoring_paths)
    test_paths_run = test_paths_from_commands(test_commands, relevant_tests)
    relevant_tests_covered = sorted((relevant_tests & read_set) | test_paths_run)

    target_file_recall_from_reads = len(target_files_read) / len(target_files) if target_files else 1.0
    target_file_recall_from_edits = len(target_files_edited) / len(target_files) if target_files else 1.0
    relevant_test_recall_from_reads = len(relevant_tests_covered) / len(relevant_tests) if relevant_tests else 1.0
    required_context_recall_from_reads = len(required_context_read) / len(required_context) if required_context else 1.0
    first_target_file_rank = next((index for index, path in enumerate(files_read_paths, start=1) if path in target_files), len(files_read_paths) + 1)
    read_bytes = sum(file_size_for_trial(repo, path) for path in files_read_paths)
    resolved_module = selection.get("resolved_module")
    expected_module_match = None if strategy == "naive" else resolved_module == row["expected_module"]
    allowed_edit_paths = target_files | relevant_tests | allowed_context | required_context
    patch_touches_only_allowed_area = edited_set.issubset(allowed_edit_paths)

    failure_reasons: list[str] = []
    if not normalized["runner_ok"]:
        failure_reasons.append(normalized["failure_reason"] or "agent-command-failed")
    if agent_final_status_failed(normalized["final_status"]):
        failure_reasons.append("agent-reported-failure")
    if target_file_recall_from_reads < 1.0:
        failure_reasons.append("missing-target-files")
    if target_files and not target_files_edited and not row.get("read_only", False):
        failure_reasons.append("target-files-not-edited")
    if relevant_test_recall_from_reads < 1.0:
        failure_reasons.append("missing-relevant-tests")
    if strategy in CONTRACT_REQUIRED_AGENT_STRATEGIES and required_context_recall_from_reads < 1.0:
        failure_reasons.append("missing-required-context")
    if misleading_files_edited:
        failure_reasons.append("misleading-file-edited")
    if expected_module_match is False and strategy in MODULE_LIKE_CONTEXT_STRATEGIES:
        failure_reasons.append("expected-module-mismatch")
    if tests_passed is False:
        failure_reasons.append("tests-failed")
    if not patch_touches_only_allowed_area:
        failure_reasons.append("patch-outside-allowed-area")
    failure_reasons = unique_ordered(failure_reasons)
    trace = build_agent_trial_trace(
        strategy,
        selection,
        normalized,
        files_read_paths,
        files_edited_paths,
        test_commands,
        failure_reasons,
    )

    return {
        "task_id": row["task_id"],
        "row_number": row["row_number"],
        "strategy": strategy,
        "mode": mode,
        "run_index": run_index,
        "run_id": agent_trial_run_id(row["task_id"], strategy, run_index),
        "resolved_module": resolved_module,
        "expected_module_match": expected_module_match,
        "files_read_count": len(files_read_paths),
        "files_read_paths": files_read_paths,
        "preloaded_context_read_paths": preloaded_paths,
        "agent_reported_files_read_paths": normalized["files_read"],
        "files_edited_count": len(files_edited_paths),
        "files_edited_paths": files_edited_paths,
        "reported_files_edited_paths": normalized["files_edited"],
        "target_file_recall_from_reads": round(target_file_recall_from_reads, 6),
        "target_file_recall_from_edits": round(target_file_recall_from_edits, 6),
        "target_files_read_paths": target_files_read,
        "target_files_edited_paths": target_files_edited,
        "relevant_test_recall_from_reads": round(relevant_test_recall_from_reads, 6),
        "relevant_test_files_read_paths": relevant_tests_read,
        "relevant_test_files_run_paths": sorted(test_paths_run),
        "required_context_recall_from_reads": round(required_context_recall_from_reads, 6),
        "required_context_read_paths": required_context_read,
        "misleading_files_read": len(misleading_files_read),
        "misleading_files_read_paths": misleading_files_read,
        "misleading_files_edited": len(misleading_files_edited),
        "misleading_files_edited_paths": misleading_files_edited,
        "irrelevant_files_read": len(irrelevant_files_read_paths),
        "irrelevant_files_read_paths": irrelevant_files_read_paths,
        "read_bytes": read_bytes,
        "first_target_file_rank": first_target_file_rank,
        "commands_run": commands,
        "tests_run": len(test_commands),
        "test_commands": test_commands,
        "tests_passed": tests_passed,
        "patch_touches_only_allowed_area": patch_touches_only_allowed_area,
        "agent_reported_final_status": normalized["final_status"],
        "agent_notes": normalized["notes"],
        "agent_success": not failure_reasons,
        "failure_reason": None if not failure_reasons else ", ".join(failure_reasons),
        "runner_failure_reason": normalized["failure_reason"] or None,
        "stdout": normalized["stdout"],
        "stderr": normalized["stderr"],
        "returncode": normalized["returncode"],
        "git_diff": git_diff,
        "trace": trace,
    }


