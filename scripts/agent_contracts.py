def utc_now() -> str:
    return dt.datetime.now(dt.timezone.utc).strftime(NOW_FORMAT)


def stable_slug(value: str) -> str:
    slug = re.sub(r"[^A-Za-z0-9]+", "-", value.strip().lower()).strip("-")
    return slug or "root"


def rel_path(path: Path, root: Path) -> str:
    return path.relative_to(root).as_posix()


def is_text_file(path: Path) -> bool:
    if path.name in MANIFEST_NAMES or path.name in CONFIG_NAMES:
        return True
    return path.suffix in TEXT_EXTENSIONS


def is_generated_path(path: str) -> bool:
    parts = set(Path(path).parts)
    return bool(parts & GENERATED_HINTS)


def safe_read(path: Path, limit: int = 500_000) -> str:
    try:
        if path.stat().st_size > limit:
            return ""
        return path.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return ""


def load_json(path: Path) -> dict[str, Any]:
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError):
        return {}


def classify_file(path: str, language: str) -> str:
    base = Path(path).name
    if base == "SPEC.md":
        return "contract"
    if base == "AGENTS.md":
        return "agent-instructions"
    if base == "ARCHITECTURE.md":
        return "architecture"
    if TEST_RE.search(path):
        return "test"
    if base in MANIFEST_NAMES:
        return "manifest"
    if base in CONFIG_NAMES or path.startswith(".github/"):
        return "config"
    if base.lower().startswith("readme") or Path(path).suffix == ".md":
        return "docs"
    if language:
        return "source"
    return "other"


def inventory(repo: Path) -> list[FileInfo]:
    files: list[FileInfo] = []
    for dirpath, dirnames, filenames in os.walk(repo):
        current = Path(dirpath)
        dirnames[:] = [
            dirname
            for dirname in sorted(dirnames)
            if dirname not in EXCLUDED_DIRS and not is_generated_path(rel_path(current / dirname, repo))
        ]
        for filename in sorted(filenames):
            absolute = current / filename
            try:
                if not absolute.is_file() or not is_text_file(absolute):
                    continue
                relative = rel_path(absolute, repo)
                if is_generated_path(relative):
                    continue
                stat = absolute.stat()
                size = stat.st_size
                mtime_ns = stat.st_mtime_ns
            except OSError:
                continue
            language = LANG_BY_EXT.get(absolute.suffix, "")
            role = classify_file(relative, language)
            files.append(FileInfo(relative, size, language, role, mtime_ns))
    return files


def prefix_match(path: str, root: str) -> bool:
    if root == ".":
        return True
    return path == root or path.startswith(root.rstrip("/") + "/")


def infer_candidate_roots(files: list[FileInfo]) -> set[str]:
    candidates: set[str] = set()
    source_paths = [file.path for file in files if file.role in {"source", "test", "manifest"}]

    for file in files:
        if file.role == "manifest":
            parent = Path(file.path).parent.as_posix()
            if parent != ".":
                candidates.add(parent)

    grouped_prefixes = {"apps", "packages", "services", "libs", "modules"}
    for path in source_paths:
        parts = Path(path).parts
        if not parts:
            continue
        if parts[0] in grouped_prefixes and len(parts) >= 2:
            candidates.add(Path(parts[0], parts[1]).as_posix())
        elif parts[0] == "src" and len(parts) >= 3:
            candidates.add(Path(parts[0], parts[1]).as_posix())
        elif parts[0] == "src" and len(parts) == 2:
            candidates.add("src")
        elif len(parts) >= 2 and parts[0] not in {"test", "tests", "spec", "specs", "docs"}:
            candidates.add(parts[0])

    if not candidates:
        candidates.add(".")

    source_counts = {
        candidate: sum(1 for file in files if file.role in {"source", "test"} and prefix_match(file.path, candidate))
        for candidate in candidates
    }
    candidates = {candidate for candidate in candidates if source_counts.get(candidate, 0) > 0}

    if not candidates:
        candidates.add(".")

    return candidates


def module_name_from_root(root: str) -> str:
    if root == ".":
        return "root"
    parts = Path(root).parts
    if len(parts) >= 2 and parts[0] in {"apps", "packages", "services", "libs", "modules"}:
        return stable_slug(parts[-1])
    return stable_slug(parts[-1])


def deepest_candidate(path: str, candidates: set[str]) -> str | None:
    matches = [candidate for candidate in candidates if prefix_match(path, candidate)]
    if not matches:
        return None
    return max(matches, key=lambda item: len(Path(item).parts) if item != "." else 0)


def parse_package_json(repo: Path, path: str) -> tuple[str | None, list[str]]:
    data = load_json(repo / path)
    name = data.get("name") if isinstance(data.get("name"), str) else None
    scripts = data.get("scripts") if isinstance(data.get("scripts"), dict) else {}
    commands = [f"npm run {key}" for key in sorted(scripts)]
    return name, commands


def parse_pyproject(repo: Path, path: str) -> tuple[str | None, list[str]]:
    if tomllib is None:
        return None, []
    try:
        data = tomllib.loads((repo / path).read_text(encoding="utf-8"))
    except (OSError, tomllib.TOMLDecodeError):
        return None, []
    project = data.get("project") if isinstance(data.get("project"), dict) else {}
    name = project.get("name") if isinstance(project.get("name"), str) else None
    scripts = project.get("scripts") if isinstance(project.get("scripts"), dict) else {}
    commands = [key for key in sorted(scripts)]
    tool = data.get("tool") if isinstance(data.get("tool"), dict) else {}
    poetry = tool.get("poetry") if isinstance(tool.get("poetry"), dict) else {}
    poetry_scripts = poetry.get("scripts") if isinstance(poetry.get("scripts"), dict) else {}
    commands.extend(key for key in sorted(poetry_scripts) if key not in commands)
    return name, commands


def extract_make_commands(repo: Path, path: str) -> list[str]:
    text = safe_read(repo / path)
    commands = []
    for match in MAKE_TARGET_RE.finditer(text):
        target = match.group(1)
        if not target.startswith("."):
            commands.append(f"make {target}")
    return sorted(set(commands))


def extract_readme_commands(repo: Path, path: str) -> list[str]:
    text = safe_read(repo / path)
    commands = []
    for match in README_COMMAND_RE.finditer(text):
        command = " ".join(match.group(1).strip().split())
        if len(command) <= 100:
            commands.append(command)
    return sorted(set(commands))[:8]


def extract_commands(repo: Path, files: list[str]) -> tuple[list[str], list[str]]:
    package_names: list[str] = []
    commands: list[str] = []
    for path in files:
        base = Path(path).name
        if base == "package.json":
            name, found = parse_package_json(repo, path)
            if name:
                package_names.append(name)
            commands.extend(found)
        elif base == "pyproject.toml":
            name, found = parse_pyproject(repo, path)
            if name:
                package_names.append(name)
            commands.extend(found)
        elif base == "Makefile":
            commands.extend(extract_make_commands(repo, path))
        elif base.lower().startswith("readme"):
            commands.extend(extract_readme_commands(repo, path))
    return sorted(set(package_names)), sorted(set(commands))


def extract_public_surfaces(repo: Path, file: FileInfo) -> list[str]:
    text = safe_read(repo / file.path)
    surfaces: list[str] = []
    if not text:
        return surfaces
    if file.language == "python":
        for method, route in PY_ROUTE_RE.findall(text):
            surfaces.append(f"route {method.upper()} {route}")
        for function_name, class_name in PY_PUBLIC_RE.findall(text):
            name = function_name or class_name
            if name and not name.startswith("_"):
                kind = "class" if class_name else "function"
                surfaces.append(f"{kind} {name}")
    elif file.language in {"javascript", "typescript"}:
        for method, route in JS_ROUTE_RE.findall(text):
            surfaces.append(f"route {method.upper()} {route}")
        for name in JS_EXPORT_RE.findall(text):
            if not name.startswith("_"):
                surfaces.append(f"export {name}")
        if "/app/" in f"/{file.path}" and Path(file.path).name in {"route.ts", "route.js"}:
            route_path = "/" + "/".join(Path(file.path).parts[1:-1])
            surfaces.append(f"route file {route_path}")
    return sorted(set(surfaces))


def py_imports(repo: Path, file: FileInfo) -> list[str]:
    text = safe_read(repo / file.path)
    if not text:
        return []
    try:
        tree = ast.parse(text)
    except SyntaxError:
        return []
    imports: list[str] = []
    for node in ast.walk(tree):
        if isinstance(node, ast.Import):
            imports.extend(alias.name for alias in node.names)
        elif isinstance(node, ast.ImportFrom):
            prefix = "." * node.level
            module = node.module or ""
            imports.append(prefix + module)
    return imports


def js_imports(repo: Path, file: FileInfo) -> list[str]:
    text = safe_read(repo / file.path)
    if not text:
        return []
    patterns = [
        re.compile(r"\bimport\s+(?:[^'\"]+\s+from\s+)?['\"]([^'\"]+)['\"]"),
        re.compile(r"\bexport\s+[^'\"]+\s+from\s+['\"]([^'\"]+)['\"]"),
        re.compile(r"\brequire\(\s*['\"]([^'\"]+)['\"]\s*\)"),
    ]
    imports: list[str] = []
    for pattern in patterns:
        imports.extend(pattern.findall(text))
    return imports


def import_strings(repo: Path, file: FileInfo) -> list[str]:
    if file.language == "python":
        return py_imports(repo, file)
    if file.language in {"javascript", "typescript"}:
        return js_imports(repo, file)
    return []


def find_existing_path(repo: Path, candidate: Path) -> str | None:
    suffixes = ["", ".py", ".js", ".jsx", ".ts", ".tsx", "/__init__.py", "/index.js", "/index.ts", "/index.tsx"]
    for suffix in suffixes:
        path = (repo / (candidate.as_posix() + suffix)).resolve()
        if path.is_file():
            try:
                return rel_path(path, repo)
            except ValueError:
                return None
    return None


def resolve_import_path(repo: Path, source: str, raw: str, package_to_module: dict[str, ModuleInfo]) -> str | None:
    source_parent = Path(source).parent
    if raw.startswith("."):
        if raw.startswith("./") or raw.startswith("../"):
            return find_existing_path(repo, source_parent / raw)
        level = len(raw) - len(raw.lstrip("."))
        module_part = raw[level:]
        base = source_parent
        for _ in range(max(level - 1, 0)):
            base = base.parent
        if module_part:
            base = base / Path(*module_part.split("."))
        return find_existing_path(repo, base)

    if raw in package_to_module:
        module = package_to_module[raw]
        return module.source_files[0] if module.source_files else module.root

    top = raw.split(".")[0].split("/")[0]
    for module in package_to_module.values():
        root_leaf = Path(module.root).name if module.root != "." else module.name
        aliases = {module.name.replace("-", "_"), root_leaf.replace("-", "_")}
        if top.replace("-", "_") in aliases:
            return module.source_files[0] if module.source_files else module.root
    return None


def derive_capabilities(module_name: str, public_surfaces: list[str], source_files: list[str]) -> list[str]:
    capabilities = [f"{module_name}-module"]
    route_count = sum(1 for surface in public_surfaces if surface.startswith("route "))
    export_count = sum(1 for surface in public_surfaces if surface.startswith(("export ", "function ", "class ")))
    if route_count:
        capabilities.append("http-api")
    if export_count:
        capabilities.append("public-api")
    if any("command" in path or "cli" in path for path in source_files):
        capabilities.append("command-interface")
    return sorted(set(capabilities))


def module_kind(files: list[FileInfo], surfaces: list[str]) -> str:
    basenames = {Path(file.path).name for file in files}
    if "package.json" in basenames or "pyproject.toml" in basenames:
        return "package"
    if any(surface.startswith("route ") for surface in surfaces):
        return "service"
    if any(file.role == "source" for file in files):
        return "component"
    return "module"


def attach_external_tests(repo: Path, modules: list[ModuleInfo], unassigned: list[FileInfo]) -> None:
    for file in unassigned:
        if file.role != "test":
            continue
        text = safe_read(repo / file.path).lower()
        path_hint = stable_slug(file.path)
        scored_matches: list[tuple[int, ModuleInfo]] = []
        for module in modules:
            aliases = {module.name, module.name.replace("-", "_")}
            if module.root != ".":
                aliases.add(Path(module.root).name.replace("-", "_"))
            aliases.update(package.replace("-", "_") for package in module.package_names)
            score = 0
            if any(alias and alias.lower() in path_hint for alias in aliases):
                score += 3
            if any(alias and re.search(rf"['\"./@-]{re.escape(alias.lower())}[/.'\"]?", text) for alias in aliases):
                score += 1
            if module.root != "." and module.root.lower() in text:
                score += 2
            if score:
                scored_matches.append((score, module))
        if scored_matches:
            scored_matches.sort(key=lambda item: item[0], reverse=True)
            if len(scored_matches) > 1 and scored_matches[0][0] == scored_matches[1][0]:
                continue
            module = scored_matches[0][1]
            if file.path not in module.owned_files:
                module.owned_files.append(file.path)
                module.owned_files.sort()
            if file.path not in module.test_files:
                module.test_files.append(file.path)
                module.test_files.sort()
            note = f"Test file `{file.path}` is outside the module root and was associated by local evidence."
            if note not in module.boundary_notes:
                module.boundary_notes.append(note)


def module_alias_map(modules: list[ModuleInfo]) -> dict[str, ModuleInfo]:
    package_to_module: dict[str, ModuleInfo] = {}
    for module in modules:
        package_to_module[module.name] = module
        package_to_module[module.name.replace("-", "_")] = module
        if module.root != ".":
            package_to_module[Path(module.root).name.replace("-", "_")] = module
        for package_name in module.package_names:
            package_to_module[package_name] = module
    return package_to_module


def module_map_cache_enabled() -> bool:
    value = os.environ.get(MODULE_MAP_CACHE_ENV, "").strip().lower()
    return value not in {"1", "true", "yes", "on"}


def git_private_cache_dir(git_root: Path) -> Path | None:
    dot_git = git_root / ".git"
    if dot_git.is_dir():
        return dot_git / GIT_MODULE_MAP_CACHE_DIR
    if not dot_git.is_file():
        return None
    text = safe_read(dot_git, limit=1_000).strip()
    prefix = "gitdir:"
    if not text.lower().startswith(prefix):
        return None
    raw_git_dir = text[len(prefix) :].strip()
    if not raw_git_dir:
        return None
    git_dir = Path(raw_git_dir)
    if not git_dir.is_absolute():
        git_dir = (git_root / git_dir).resolve()
    return git_dir / GIT_MODULE_MAP_CACHE_DIR


def module_map_cache_path(repo: Path) -> Path:
    resolved_repo = repo.resolve()
    git_root = find_git_root(resolved_repo)
    cache_dir = git_private_cache_dir(git_root) if git_root else None
    if cache_dir is None:
        cache_dir = resolved_repo / MODULE_MAP_CACHE_DIR
    repo_digest = hashlib.sha1(resolved_repo.as_posix().encode("utf-8")).hexdigest()[:12]
    return cache_dir / f"module-map-{repo_digest}.json"


def module_map_fingerprint(repo: Path, files: list[FileInfo]) -> dict[str, Any]:
    digest = hashlib.sha256()
    digest.update(MODULE_MAP_CACHE_ALGORITHM.encode("utf-8"))
    digest.update(b"\0")
    digest.update(repo.resolve().as_posix().encode("utf-8"))
    for file in sorted(files, key=lambda item: item.path):
        digest.update(b"\0")
        digest.update(file.path.encode("utf-8", errors="surrogateescape"))
        digest.update(f":{file.size}:{file.mtime_ns}:{file.language}:{file.role}".encode("utf-8"))
    return {
        "algorithm": MODULE_MAP_CACHE_ALGORITHM,
        "file_count": len(files),
        "digest": digest.hexdigest(),
    }


def read_cached_module_map(repo: Path, fingerprint: dict[str, Any]) -> dict[str, Any] | None:
    if not module_map_cache_enabled():
        return None
    cache_path = module_map_cache_path(repo)
    cache_key = cache_path.as_posix()
    memory_hit = _MODULE_MAP_MEMORY_CACHE.get(cache_key)
    if memory_hit and memory_hit.get("fingerprint") == fingerprint:
        return memory_hit.get("map_data")
    try:
        payload = json.loads(cache_path.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError):
        return None
    if payload.get("schema_version") != MODULE_MAP_CACHE_SCHEMA_VERSION:
        return None
    if payload.get("fingerprint") != fingerprint:
        return None
    map_data = payload.get("map_data")
    if not isinstance(map_data, dict):
        return None
    _MODULE_MAP_MEMORY_CACHE[cache_key] = {"fingerprint": fingerprint, "map_data": map_data}
    return map_data


def write_cached_module_map(repo: Path, fingerprint: dict[str, Any], map_data: dict[str, Any]) -> None:
    if not module_map_cache_enabled():
        return
    cache_path = module_map_cache_path(repo)
    payload = {
        "schema_version": MODULE_MAP_CACHE_SCHEMA_VERSION,
        "product": PRODUCT,
        "created_at": utc_now(),
        "repo_root": repo.resolve().as_posix(),
        "fingerprint": fingerprint,
        "map_data": map_data,
    }
    try:
        cache_path.parent.mkdir(parents=True, exist_ok=True)
        temporary = cache_path.with_suffix(cache_path.suffix + ".tmp")
        temporary.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")
        temporary.replace(cache_path)
    except OSError:
        return
    _MODULE_MAP_MEMORY_CACHE[cache_path.as_posix()] = {"fingerprint": fingerprint, "map_data": map_data}


def clear_module_map_cache() -> None:
    _MODULE_MAP_MEMORY_CACHE.clear()


def build_module_map(repo: Path, *, use_cache: bool = True) -> dict[str, Any]:
    repo = repo.resolve()
    files = inventory(repo)
    fingerprint = module_map_fingerprint(repo, files)
    if use_cache:
        cached = read_cached_module_map(repo, fingerprint)
        if cached is not None:
            return cached
    map_data = build_module_map_from_files(repo, files)
    if use_cache:
        write_cached_module_map(repo, fingerprint, map_data)
    return map_data


def build_module_map_from_files(repo: Path, files: list[FileInfo]) -> dict[str, Any]:
    candidates = infer_candidate_roots(files)
    assigned: dict[str, list[FileInfo]] = {candidate: [] for candidate in candidates}
    root_bucket: list[FileInfo] = []
    unassigned: list[FileInfo] = []

    for file in files:
        candidate = deepest_candidate(file.path, candidates)
        if candidate:
            assigned[candidate].append(file)
        elif file.role in {"manifest", "config", "docs", "architecture", "agent-instructions"}:
            root_bucket.append(file)
        else:
            unassigned.append(file)

    if root_bucket and "." not in assigned:
        assigned["."] = root_bucket
    elif root_bucket:
        assigned["."].extend(root_bucket)

    modules: list[ModuleInfo] = []
    used_names: dict[str, int] = {}
    for root in sorted(assigned, key=lambda item: (item == ".", item)):
        module_files = sorted(assigned[root], key=lambda file: file.path)
        if not module_files:
            continue
        base_name = module_name_from_root(root)
        used_names[base_name] = used_names.get(base_name, 0) + 1
        name = base_name if used_names[base_name] == 1 else f"{base_name}-{used_names[base_name]}"
        owned_files = [file.path for file in module_files]
        source_files = [file.path for file in module_files if file.role == "source"]
        test_files = [file.path for file in module_files if file.role == "test"]
        docs = [file.path for file in module_files if file.role in {"docs", "architecture"}]
        configs = [file.path for file in module_files if file.role == "config"]
        manifests = [file.path for file in module_files if file.role == "manifest"]
        package_names, commands = extract_commands(repo, owned_files)
        public_surfaces: list[str] = []
        for file in module_files:
            if file.role == "source":
                public_surfaces.extend(extract_public_surfaces(repo, file))
        public_surfaces = sorted(set(public_surfaces))
        confidence = 0.55
        if manifests:
            confidence += 0.2
        if source_files:
            confidence += 0.1
        if test_files:
            confidence += 0.1
        if docs:
            confidence += 0.05
        confidence = min(confidence, 0.98)
        notes: list[str] = []
        if not manifests:
            notes.append("No package manifest anchors this boundary.")
        if root == "." and len(candidates) > 1:
            notes.append("Root files support multiple modules and may contain shared repository instructions.")
        modules.append(
            ModuleInfo(
                name=name,
                root=root,
                kind=module_kind(module_files, public_surfaces),
                owned_files=owned_files,
                source_files=source_files,
                test_files=test_files,
                docs=docs,
                configs=configs,
                manifests=manifests,
                commands=commands,
                public_surfaces=public_surfaces,
                dependencies=[],
                imports=[],
                package_names=package_names,
                capabilities=derive_capabilities(name, public_surfaces, source_files),
                confidence=round(confidence, 2),
                boundary_notes=notes,
            )
        )

    attach_external_tests(repo, modules, unassigned)

    package_to_module = module_alias_map(modules)

    path_to_module: dict[str, ModuleInfo] = {}
    for module in modules:
        for path in module.owned_files:
            path_to_module[path] = module

    for module in modules:
        imports: list[ImportRef] = []
        dependencies: set[str] = set()
        source_infos = [file for file in files if file.path in set(module.source_files)]
        for file in source_infos:
            for raw in import_strings(repo, file):
                resolved = resolve_import_path(repo, file.path, raw, package_to_module)
                target_module = None
                is_internal = False
                if resolved:
                    target = deepest_candidate(resolved, {item.root for item in modules})
                    target_module = next((item.name for item in modules if item.root == target), None)
                    if target_module and target_module != module.name:
                        dependencies.add(target_module)
                        is_internal = "/internal/" in f"/{resolved}" or "/private/" in f"/{resolved}"
                imports.append(ImportRef(file.path, raw, resolved, target_module, is_internal))
        module.imports = sorted(imports, key=lambda item: (item.source, item.raw))
        module.dependencies = sorted(dependencies)

    return {
        "schema_version": 1,
        "product": PRODUCT,
        "generated_at": utc_now(),
        "repo_root": repo.as_posix(),
        "modules": [module.to_dict() for module in modules],
        "summary": {
            "file_count": len(files),
            "module_count": len(modules),
            "read_only": True,
        },
    }


def module_from_dict(data: dict[str, Any]) -> ModuleInfo:
    imports = [ImportRef(**item) for item in data.get("imports", [])]
    values = dict(data)
    values["imports"] = imports
    return ModuleInfo(**values)


