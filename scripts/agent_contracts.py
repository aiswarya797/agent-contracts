def render_findings_text(findings: list[Finding]) -> str:
    if not findings:
        return "No drift findings detected.\n"
    lines = [f"{PRODUCT} drift report", ""]
    order = {"high": 0, "medium": 1, "low": 2}
    for index, finding in enumerate(sorted(findings, key=lambda item: (order.get(item.severity, 9), item.code)), 1):
        lines.extend(
            [
                f"## {index}. [{finding.severity.upper()}] {finding.title}",
                f"- code: {finding.code}",
                f"- evidence: {finding.evidence}",
                f"- affected files: {', '.join(path for path in finding.affected_files if path) or 'none'}",
                f"- local fix: {finding.local_fix}",
                "",
            ]
        )
    return "\n".join(lines)


