def agent_trial_metric_value(source: dict[str, Any], metric: str) -> float | None:
    value = source.get(metric)
    if value is None:
        return None
    if isinstance(value, bool):
        return 1.0 if value else 0.0
    if isinstance(value, (int, float)):
        return float(value)
    return None


def compare_agent_trial_runs(left: dict[str, Any], right: dict[str, Any]) -> dict[str, float | None]:
    deltas: dict[str, float | None] = {}
    for _, run_metric in AGENT_TRIAL_AGGREGATE_METRICS:
        left_value = agent_trial_metric_value(left, run_metric)
        right_value = agent_trial_metric_value(right, run_metric)
        deltas[run_metric] = None if left_value is None or right_value is None else round(left_value - right_value, 6)
    for metric, output in [
        ("misleading_files_read", "misleading_files_read_reduction"),
        ("misleading_files_edited", "misleading_files_edited_reduction"),
        ("irrelevant_files_read", "irrelevant_files_read_reduction"),
        ("files_read_count", "files_read_reduction"),
        ("files_edited_count", "files_edited_reduction"),
        ("read_bytes", "read_bytes_savings"),
        ("first_target_file_rank", "first_target_rank_improvement"),
    ]:
        left_value = agent_trial_metric_value(left, metric)
        right_value = agent_trial_metric_value(right, metric)
        deltas[output] = None if left_value is None or right_value is None else round(right_value - left_value, 6)
    return deltas


