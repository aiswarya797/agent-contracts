    init_parser.add_argument("--overwrite-existing", action="store_true", help="Allow overwriting existing generated paths")
    init_parser.set_defaults(func=cmd_init)

    check_parser = subparsers.add_parser("check", help="Read-only drift check")
    check_parser.add_argument("--repo", default=".", help="Repository to inspect")
    check_parser.add_argument("--format", choices=["text", "json"], default="text")
    check_parser.add_argument("--fail-on", choices=["high", "medium", "low"], help="Exit 1 when a finding has this severity")
    check_parser.set_defaults(func=cmd_check)

    refresh_parser = subparsers.add_parser("refresh", help="Plan safe and contract-changing updates")
    refresh_parser.add_argument("--repo", default=".", help="Repository to inspect")
    refresh_parser.add_argument("--write-safe", action="store_true", help="Write safe instruction refreshes")
    refresh_parser.add_argument("--write-contract", action="store_true", help="Write contract-changing updates after approval")
    refresh_parser.add_argument("--yes", action="store_true", help="Approve requested writes")
