def comparable_metric(source: dict[str, Any], metric: str) -> float | None:
    if metric == "pass_rate" and "passed" in source:
        return 1.0 if source["passed"] else 0.0
    value = source.get(metric)
    if value is None:
        return None
    if isinstance(value, bool):
        return 1.0 if value else 0.0
    if isinstance(value, (int, float)):
        return float(value)
    return None


def comparison_key(left: str, right: str) -> str:
    return f"{left.replace('-', '_')}_vs_{right.replace('-', '_')}"


def compare_strategy_metrics(left: dict[str, Any], right: dict[str, Any]) -> dict[str, Any]:
    deltas: dict[str, float | None] = {}
    for metric in COMPARISON_METRICS:
        left_value = comparable_metric(left, metric)
        right_value = comparable_metric(right, metric)
        deltas[metric] = None if left_value is None or right_value is None else round(left_value - right_value, 6)

    left_bytes = comparable_metric(left, "selected_bytes")
    right_bytes = comparable_metric(right, "selected_bytes")
    byte_savings = None if left_bytes is None or right_bytes is None else round(right_bytes - left_bytes, 6)
    savings_rate = None
    if byte_savings is not None and right_bytes and right_bytes > 0:
        savings_rate = round(byte_savings / right_bytes, 6)
    deltas["selected_bytes_savings"] = byte_savings
    deltas["selected_bytes_savings_rate"] = savings_rate

    for metric, output in [
        ("misleading_files_included", "misleading_files_reduction"),
        ("irrelevant_files_read", "irrelevant_files_reduction"),
        ("selected_file_count", "selected_file_count_reduction"),
        ("context_bloat", "context_bloat_reduction"),
        ("first_target_file_rank", "first_target_rank_improvement"),
    ]:
        left_value = comparable_metric(left, metric)
        right_value = comparable_metric(right, metric)
        deltas[output] = None if left_value is None or right_value is None else round(right_value - left_value, 6)
    return deltas


def build_context_comparisons(tasks: list[dict[str, Any]], aggregate: dict[str, Any]) -> dict[str, Any]:
    aggregate_comparisons: dict[str, Any] = {}
    for left, right in CONTEXT_BENCHMARK_COMPARISONS:
        aggregate_comparisons[comparison_key(left, right)] = {
            "left": left,
            "right": right,
            "deltas": compare_strategy_metrics(aggregate[left], aggregate[right]),
        }

    task_comparisons: list[dict[str, Any]] = []
    for task in tasks:
        comparisons: dict[str, Any] = {}
        for left, right in CONTEXT_BENCHMARK_COMPARISONS:
            comparisons[comparison_key(left, right)] = {
                "left": left,
                "right": right,
                "deltas": compare_strategy_metrics(task["strategies"][left], task["strategies"][right]),
            }
        task_comparisons.append({"task_id": task["task_id"], "comparisons": comparisons})
    return {
        "aggregate": aggregate_comparisons,
        "tasks": task_comparisons,
        "delta_note": "Positive recall/pass deltas favor left. Positive reduction/savings/improvement values mean left read less or ranked targets earlier than right.",
    }


def trial_comparable_metric(source: dict[str, Any], aggregate_metric: str, task_metric: str) -> float | None:
    if aggregate_metric in source:
        value = source.get(aggregate_metric)
    elif task_metric == "trial_success":
        value = 1.0 if source.get("trial_success") else 0.0
    else:
        value = source.get(task_metric)
    if value is None:
        return None
    if isinstance(value, bool):
        return 1.0 if value else 0.0
    if isinstance(value, (int, float)):
        return float(value)
    return None


def compare_trial_metrics(left: dict[str, Any], right: dict[str, Any]) -> dict[str, float | None]:
    deltas: dict[str, float | None] = {}
    for aggregate_metric, task_metric in TRIAL_COMPARISON_METRICS:
        left_value = trial_comparable_metric(left, aggregate_metric, task_metric)
        right_value = trial_comparable_metric(right, aggregate_metric, task_metric)
        deltas[aggregate_metric] = None if left_value is None or right_value is None else round(left_value - right_value, 6)

    reduction_metrics = [
        ("average_files_read", "files_read_reduction"),
        ("average_files_edited", "files_edited_reduction"),
        ("average_misleading_files_read", "misleading_files_read_reduction"),
        ("average_misleading_files_edited", "misleading_files_edited_reduction"),
        ("average_read_bytes", "read_bytes_savings"),
        ("average_first_target_rank", "first_target_rank_improvement"),
    ]
    for metric, output in reduction_metrics:
        task_metric = next(task for aggregate, task in TRIAL_COMPARISON_METRICS if aggregate == metric)
        left_value = trial_comparable_metric(left, metric, task_metric)
        right_value = trial_comparable_metric(right, metric, task_metric)
        deltas[output] = None if left_value is None or right_value is None else round(right_value - left_value, 6)
    return deltas


