def parse_frontmatter(text: str) -> dict[str, Any]:
    if not text.startswith("---\n"):
        return {}
    end = text.find("\n---", 4)
    if end == -1:
        return {}
    lines = text[4:end].splitlines()
    data: dict[str, Any] = {}
    current_key: str | None = None
    for line in lines:
        if not line.strip():
            continue
        if not line.startswith(" ") and ":" in line:
            key, value = line.split(":", 1)
            key = key.strip()
            value = value.strip()
            if value == "[]":
                data[key] = []
                current_key = key
            elif value:
                data[key] = value.strip("'\"")
                current_key = None
            else:
                data[key] = []
                current_key = key
        elif current_key and line.strip().startswith("- "):
            data.setdefault(current_key, []).append(line.strip()[2:].strip("'\""))
    return data


def specs_in_repo(repo: Path) -> dict[str, dict[str, Any]]:
    specs: dict[str, dict[str, Any]] = {}
    for path in repo.rglob("SPEC.md"):
        if any(part in EXCLUDED_DIRS for part in path.relative_to(repo).parts):
            continue
        text = safe_read(path)
        frontmatter = parse_frontmatter(text)
        module_name = str(frontmatter.get("module") or stable_slug(path.parent.name if path.parent != repo else "root"))
        specs[module_name] = {
            "path": rel_path(path, repo),
            "root": "." if path.parent == repo else rel_path(path.parent, repo),
            "frontmatter": frontmatter,
            "text": text,
        }
    return specs


def path_matches_declared(path: str, patterns: list[str]) -> bool:
    for pattern in patterns:
        normalized = pattern.strip()
        if normalized == "*":
            return "/" not in path
        if fnmatch.fnmatch(path, normalized):
            return True
        if normalized.endswith("/**") and prefix_match(path, normalized[:-3]):
            return True
    return False


def check_drift(repo: Path) -> list[Finding]:
    map_data = build_module_map(repo)
    modules = modules_from_map(map_data)
    specs = specs_in_repo(repo)
    findings: list[Finding] = []

    declared_paths: list[str] = []
    for spec in specs.values():
        owned = spec["frontmatter"].get("owned_paths", [])
        if isinstance(owned, list):
            declared_paths.extend(owned)

    if declared_paths:
        for file in inventory(repo):
            if file.role in {"contract", "agent-instructions", "architecture"}:
                continue
            if not path_matches_declared(file.path, declared_paths):
                findings.append(
                    Finding(
                        "medium",
                        "orphan-file",
                        "File is not covered by any declared module path",
                        f"`{file.path}` does not match any SPEC.md owned_paths entry.",
                        [file.path],
                        "Assign the file to a module SPEC.md or move it under an owned path.",
                    )
                )

    for module in modules:
        spec = specs.get(module.name)
        if not spec:
            findings.append(
                Finding(
                    "medium",
                    "missing-spec",
                    "Detected module has no SPEC.md",
                    f"`{module.name}` was detected at `{module.root}` but no matching SPEC.md was found.",
                    module.owned_files[:8],
                    "Run contract init or add a reviewed SPEC.md for this module.",
                )
            )
            continue

        frontmatter = spec["frontmatter"]
        declared_deps = set(frontmatter.get("depends_on", []) if isinstance(frontmatter.get("depends_on"), list) else [])
        actual_deps = set(module.dependencies)
        for dep in sorted(actual_deps - declared_deps):
            findings.append(
                Finding(
                    "high",
                    "undeclared-dependency",
                    "Code imports another module that is not declared",
                    f"`{module.name}` imports `{dep}` but `{dep}` is absent from depends_on.",
                    [item.source for item in module.imports if item.target_module == dep][:8],
                    f"Add `{dep}` to `{spec['path']}` depends_on or remove the cross-module import.",
                )
            )
        for dep in sorted(declared_deps):
            if dep not in specs:
                findings.append(
                    Finding(
                        "medium",
                        "missing-dependency-contract",
                        "Declared dependency has no contract",
                        f"`{module.name}` declares `{dep}`, but no `{dep}` SPEC.md was found.",
                        [spec["path"]],
                        f"Create a SPEC.md for `{dep}` or remove the stale dependency.",
                    )
                )

        for item in module.imports:
            if item.target_module and item.target_module != module.name and item.is_internal:
                findings.append(
                    Finding(
                        "high",
                        "internal-import",
                        "Module imports another module's internal file",
                        f"`{item.source}` imports `{item.raw}`, resolved to `{item.resolved_path}`.",
                        [item.source, item.resolved_path or ""],
                        "Replace the import with a public surface from the dependency module.",
                    )
                )

        spec_text = spec["text"].lower()
        for surface in module.public_surfaces:
            if surface.lower() not in spec_text:
                findings.append(
                    Finding(
                        "medium",
                        "undeclared-public-surface",
                        "Detected public surface is not listed in SPEC.md",
                        f"`{surface}` was detected for `{module.name}` but is absent from `{spec['path']}`.",
                        module.source_files[:8],
                        "Review the surface and add it to the SPEC.md or make it internal.",
                    )
                )

        declared_capabilities = frontmatter.get("capabilities", [])
        if isinstance(declared_capabilities, list) and declared_capabilities and not module.test_files:
            findings.append(
                Finding(
                    "low",
                    "missing-test-evidence",
                    "Declared capabilities have no detected test evidence",
                    f"`{module.name}` declares capabilities but no test files were detected under the module.",
                    [spec["path"]],
                    "Add local tests or record manual verification evidence in the SPEC.md.",
                )
            )

        agents_path = Path(spec["path"]).with_name("AGENTS.md")
        if module.root == ".":
            agents_path = Path("AGENTS.md")
        if not (repo / agents_path).exists():
            findings.append(
                Finding(
                    "medium",
                    "missing-agents",
                    "Module has no AGENTS.md instructions",
                    f"`{agents_path.as_posix()}` was not found for `{module.name}`.",
                    [spec["path"]],
                    "Generate or add module agent instructions with scope, commands, tests, and dependency rules.",
                )
            )
        else:
            agents_text = safe_read(repo / agents_path)
            required = ["Module Scope", "Start Here", "Dependency Rules"]
            missing = [section for section in required if section not in agents_text]
            if missing:
                findings.append(
                    Finding(
                        "low",
                        "stale-agents",
                        "AGENTS.md is missing expected instruction sections",
                        f"`{agents_path.as_posix()}` is missing: {', '.join(missing)}.",
                        [agents_path.as_posix()],
                        "Refresh AGENTS.md safe instruction sections from current code evidence.",
                    )
                )
    return findings


