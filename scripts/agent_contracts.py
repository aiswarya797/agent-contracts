def normalize_context_path(value: str) -> str:
    normalized = str(value).strip().replace("\\", "/")
    while normalized.startswith("./"):
        normalized = normalized[2:]
    return normalized.strip("/")


def normalize_context_list(value: Any, field: str) -> tuple[list[str], list[str]]:
    if not isinstance(value, list):
        return [], [f"`{field}` must be a list of paths"]
    result: list[str] = []
    errors: list[str] = []
    for index, item in enumerate(value):
        if not isinstance(item, str) or not item.strip():
            errors.append(f"`{field}[{index}]` must be a non-empty string")
            continue
        result.append(normalize_context_path(item))
    return result, errors


def normalize_string_list(value: Any, field: str) -> tuple[list[str], list[str]]:
    if not isinstance(value, list):
        return [], [f"`{field}` must be a list of strings"]
    result: list[str] = []
    errors: list[str] = []
    for index, item in enumerate(value):
        if not isinstance(item, str) or not item.strip():
            errors.append(f"`{field}[{index}]` must be a non-empty string")
            continue
        result.append(item.strip())
    return result, errors


def normalize_context_selection_row(raw: dict[str, Any], row_number: int) -> tuple[dict[str, Any], list[str]]:
    errors: list[str] = []
    missing = sorted(field for field in CONTEXT_VERIFIER_REQUIRED_FIELDS if field not in raw)
    if missing:
        errors.append("missing required field(s): " + ", ".join(missing))

    row: dict[str, Any] = {"row_number": row_number}
    for field in ("task_id", "repo_path", "task", "expected_module"):
        value = raw.get(field)
        if not isinstance(value, str) or not value.strip():
            errors.append(f"`{field}` must be a non-empty string")
            row[field] = ""
        else:
            row[field] = value.strip()

    for field in ("target_files", "relevant_tests", "allowed_context", "misleading_files"):
        values, field_errors = normalize_context_list(raw.get(field), field)
        errors.extend(field_errors)
        row[field] = values

    required_context_raw = raw.get("required_context", [])
    values, field_errors = normalize_context_list(required_context_raw, "required_context")
    errors.extend(field_errors)
    row["required_context"] = values

    graph_seed_hints, field_errors = normalize_string_list(raw.get("graph_seed_hints", []), "graph_seed_hints")
    errors.extend(field_errors)
    row["graph_seed_hints"] = graph_seed_hints

    forbidden_paths, field_errors = normalize_context_list(raw.get("forbidden_paths", []), "forbidden_paths")
    errors.extend(field_errors)
    row["forbidden_paths"] = forbidden_paths

    expected_progressive_steps, field_errors = normalize_string_list(
        raw.get("expected_progressive_steps", []),
        "expected_progressive_steps",
    )
    errors.extend(field_errors)
    row["expected_progressive_steps"] = expected_progressive_steps

    verification_command = raw.get("verification_command", "")
    if verification_command is not None and not isinstance(verification_command, str):
        errors.append("`verification_command` must be a string when present")
    row["verification_command"] = verification_command.strip() if isinstance(verification_command, str) else ""
    if not row.get("target_files"):
        errors.append("`target_files` must include at least one file")
    return row, errors


def load_context_selection_manifest(path: Path) -> list[dict[str, Any]]:
    if not path.exists():
        raise ContextVerifierError(f"manifest not found: {path}")
    rows: list[dict[str, Any]] = []
    errors: list[str] = []
    for row_number, line in enumerate(path.read_text(encoding="utf-8").splitlines(), start=1):
        if not line.strip():
            continue
        try:
            raw = json.loads(line)
        except json.JSONDecodeError as exc:
            errors.append(f"row {row_number}: invalid JSON: {exc.msg}")
            continue
        if not isinstance(raw, dict):
            errors.append(f"row {row_number}: row must be a JSON object")
            continue
        normalized, row_errors = normalize_context_selection_row(raw, row_number)
        if row_errors:
            errors.extend(f"row {row_number}: {error}" for error in row_errors)
        else:
            rows.append(normalized)
    if not rows and not errors:
        errors.append("manifest is empty")
    if errors:
        raise ContextVerifierError("\n".join(errors))
    return rows


def resolve_context_fixture_repo(base_repo: Path, row: dict[str, Any]) -> Path:
    raw_path = Path(row["repo_path"])
    repo = raw_path if raw_path.is_absolute() else base_repo / raw_path
    repo = repo.resolve()
    if not repo.exists() or not repo.is_dir():
        raise ContextVerifierError(
            f"row {row['row_number']} ({row['task_id']}): repo_path `{row['repo_path']}` does not resolve to a directory under `{base_repo}`"
        )
    return repo


