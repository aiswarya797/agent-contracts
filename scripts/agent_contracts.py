def agent_trial_outcome_delta(left: dict[str, Any], right: dict[str, Any]) -> float:
    return (1.0 if left.get("agent_success") else 0.0) - (1.0 if right.get("agent_success") else 0.0)


def build_agent_context_trial_comparisons(
    raw_runs: list[dict[str, Any]],
    aggregate: dict[str, Any],
    strategies: tuple[str, ...],
) -> dict[str, Any]:
    aggregate_comparisons: dict[str, Any] = {}
    indexed_runs = {
        (run["task_id"], run["run_index"], run["strategy"]): run
        for run in raw_runs
    }
    for left, right in AGENT_CONTEXT_TRIAL_COMPARISONS:
        if left not in strategies or right not in strategies:
            continue
        paired_deltas: list[dict[str, Any]] = []
        wins = 0
        losses = 0
        ties = 0
        pair_keys = sorted(
            {
                (run["task_id"], run["run_index"])
                for run in raw_runs
                if run["strategy"] in {left, right}
            }
        )
        for task_id, run_index in pair_keys:
            left_run = indexed_runs.get((task_id, run_index, left))
            right_run = indexed_runs.get((task_id, run_index, right))
            if not left_run or not right_run:
                continue
            deltas = compare_agent_trial_runs(left_run, right_run)
            outcome_delta = agent_trial_outcome_delta(left_run, right_run)
            if outcome_delta > 0:
                wins += 1
                outcome = "win"
            elif outcome_delta < 0:
                losses += 1
                outcome = "loss"
            else:
                ties += 1
                outcome = "tie"
            paired_deltas.append(
                {
                    "task_id": task_id,
                    "run_index": run_index,
                    "left_run_id": left_run["run_id"],
                    "right_run_id": right_run["run_id"],
                    "outcome": outcome,
                    "deltas": deltas,
                }
            )
        aggregate_comparisons[comparison_key(left, right)] = {
            "left": left,
            "right": right,
            "run_count": len(paired_deltas),
            "wins": wins,
            "losses": losses,
            "ties": ties,
            "average_deltas": compare_agent_trial_aggregates(aggregate[left], aggregate[right]),
            "paired_deltas": paired_deltas,
        }
    return {
        "aggregate": aggregate_comparisons,
        "delta_note": "Positive recall/success deltas favor left. Positive reductions/savings/improvements mean left read, edited, or ranked less than right.",
    }


def exact_two_sided_sign_test_p_value(wins: int, losses: int) -> float | None:
    trials = wins + losses
    if trials == 0:
        return None
    tail = min(wins, losses)
    probability = sum(math.comb(trials, index) for index in range(0, tail + 1)) / (2**trials)
    return round(min(1.0, probability * 2), 6)


def deterministic_bootstrap_seed(comparison: str, metric: str) -> int:
    digest = hashlib.sha256(f"{comparison}:{metric}:{AGENT_TRIAL_BOOTSTRAP_SEED}".encode("utf-8")).hexdigest()
    return int(digest[:16], 16)


def bootstrap_mean_ci(
    values: list[float],
    *,
    seed: int,
    iterations: int = AGENT_TRIAL_BOOTSTRAP_ITERATIONS,
    confidence: float = 0.95,
) -> dict[str, Any]:
    if not values:
        return {
            "confidence": confidence,
            "iterations": iterations,
            "seed": seed,
            "lower": None,
            "upper": None,
        }
    rng = random.Random(seed)
    sample_count = len(values)
    means: list[float] = []
    for _ in range(iterations):
        total = 0.0
        for _ in range(sample_count):
            total += values[rng.randrange(sample_count)]
        means.append(total / sample_count)
    means.sort()
    lower_index = int(((1.0 - confidence) / 2.0) * (iterations - 1))
    upper_index = int((1.0 - ((1.0 - confidence) / 2.0)) * (iterations - 1))
    return {
        "confidence": confidence,
        "iterations": iterations,
        "seed": seed,
        "lower": round(means[lower_index], 6),
        "upper": round(means[upper_index], 6),
    }


def average_run_delta(observations: list[dict[str, Any]], metric: str) -> float | None:
    values = [float(item[metric]) for item in observations if item.get(metric) is not None]
    return average_metric(values) if values else None


def sample_strength_label(n_pairs: int, non_tie_success_pairs: int) -> str:
    if n_pairs < AGENT_TRIAL_DIRECTIONAL_MIN_PAIRS:
        return "insufficient_sample"
    if n_pairs < AGENT_TRIAL_SIGNIFICANCE_MIN_PAIRS or non_tie_success_pairs < AGENT_TRIAL_SIGN_TEST_MIN_NON_TIES:
        return "directional_only"
    return "statistical_test_ready"


def significance_label(p_value: float | None, n_pairs: int, non_tie_success_pairs: int) -> str:
    sample_label = sample_strength_label(n_pairs, non_tie_success_pairs)
    if sample_label == "insufficient_sample":
        return "insufficient_sample"
    if sample_label == "directional_only":
        return "directional_only"
    if p_value is None:
        return "insufficient_sample"
    return "significant" if p_value <= 0.05 else "not_significant"


def paired_statistical_observation(left_run: dict[str, Any], right_run: dict[str, Any]) -> dict[str, Any]:
    return {
        "task_id": left_run["task_id"],
        "run_index": left_run["run_index"],
        "left_run_id": left_run["run_id"],
        "right_run_id": right_run["run_id"],
        "success_delta": (1.0 if left_run["agent_success"] else 0.0) - (1.0 if right_run["agent_success"] else 0.0),
        "misleading_read_delta": float(left_run["misleading_files_read"]) - float(right_run["misleading_files_read"]),
        "misleading_edit_delta": float(left_run["misleading_files_edited"]) - float(right_run["misleading_files_edited"]),
        "files_read_delta": float(left_run["files_read_count"]) - float(right_run["files_read_count"]),
        "read_bytes_delta": float(left_run["read_bytes"]) - float(right_run["read_bytes"]),
        "required_context_recall_delta": float(left_run["required_context_recall_from_reads"])
        - float(right_run["required_context_recall_from_reads"]),
        "target_read_recall_delta": float(left_run["target_file_recall_from_reads"])
        - float(right_run["target_file_recall_from_reads"]),
    }


def build_agent_context_trial_statistical_analysis(
    raw_runs: list[dict[str, Any]],
    strategies: tuple[str, ...],
) -> dict[str, Any]:
    indexed_runs = {
        (run["task_id"], run["run_index"], run["strategy"]): run
        for run in raw_runs
    }
    comparisons: dict[str, Any] = {}
    for left, right in AGENT_CONTEXT_TRIAL_COMPARISONS:
        if left not in strategies or right not in strategies:
            continue
        key = comparison_key(left, right)
        observations: list[dict[str, Any]] = []
        pair_keys = sorted(
            {
                (run["task_id"], run["run_index"])
                for run in raw_runs
                if run["strategy"] in {left, right}
            }
        )
        for task_id, run_index in pair_keys:
            left_run = indexed_runs.get((task_id, run_index, left))
            right_run = indexed_runs.get((task_id, run_index, right))
            if left_run and right_run:
                observations.append(paired_statistical_observation(left_run, right_run))

        success_wins = sum(1 for item in observations if item["success_delta"] > 0)
        success_losses = sum(1 for item in observations if item["success_delta"] < 0)
        success_ties = sum(1 for item in observations if item["success_delta"] == 0)
        non_tie_success_pairs = success_wins + success_losses
        p_value = exact_two_sided_sign_test_p_value(success_wins, success_losses)
        n_pairs = len(observations)
        sample_label = sample_strength_label(n_pairs, non_tie_success_pairs)
        confidence_intervals = {
            "success_rate_delta": bootstrap_mean_ci(
                [float(item["success_delta"]) for item in observations],
                seed=deterministic_bootstrap_seed(key, "success_rate_delta"),
            ),
            "misleading_edit_delta": bootstrap_mean_ci(
                [float(item["misleading_edit_delta"]) for item in observations],
                seed=deterministic_bootstrap_seed(key, "misleading_edit_delta"),
            ),
            "files_read_delta": bootstrap_mean_ci(
                [float(item["files_read_delta"]) for item in observations],
                seed=deterministic_bootstrap_seed(key, "files_read_delta"),
            ),
        }
        comparisons[key] = {
            "left": left,
            "right": right,
            "n_pairs": n_pairs,
            "success_wins": success_wins,
            "success_losses": success_losses,
            "success_ties": success_ties,
            "success_rate_delta": average_run_delta(observations, "success_delta"),
            "misleading_read_delta": average_run_delta(observations, "misleading_read_delta"),
            "misleading_edit_delta": average_run_delta(observations, "misleading_edit_delta"),
            "files_read_delta": average_run_delta(observations, "files_read_delta"),
            "read_bytes_delta": average_run_delta(observations, "read_bytes_delta"),
            "required_context_recall_delta": average_run_delta(observations, "required_context_recall_delta"),
            "target_read_recall_delta": average_run_delta(observations, "target_read_recall_delta"),
            "confidence_intervals": confidence_intervals,
            "sign_test": {
                "method": "exact_two_sided_binomial_sign_test",
                "wins": success_wins,
                "losses": success_losses,
                "ties_excluded": success_ties,
                "p_value": p_value,
                "non_tie_pairs": non_tie_success_pairs,
                "minimum_non_tie_pairs": AGENT_TRIAL_SIGN_TEST_MIN_NON_TIES,
                "result": significance_label(p_value, n_pairs, non_tie_success_pairs),
            },
            "sample_label": sample_label,
            "minimum_pairs_for_significance_claim": AGENT_TRIAL_SIGNIFICANCE_MIN_PAIRS,
            "paired_observations": observations,
        }
    return {
        "method": "paired_task_run_deltas_with_deterministic_bootstrap_and_exact_sign_test",
        "bootstrap_iterations": AGENT_TRIAL_BOOTSTRAP_ITERATIONS,
        "bootstrap_seed": AGENT_TRIAL_BOOTSTRAP_SEED,
        "delta_note": "Deltas are left minus right. Positive success/recall deltas favor left; negative read/edit/byte deltas mean left used less context.",
        "comparisons": comparisons,
    }


