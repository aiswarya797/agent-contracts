def unique_ordered(items: list[str]) -> list[str]:
    seen: set[str] = set()
    result: list[str] = []
    for item in items:
        if item in seen:
            continue
        seen.add(item)
        result.append(item)
    return result


def token_matches(candidate: str, token: str) -> bool:
    if candidate == token:
        return True
    return candidate.startswith(token) or token.startswith(candidate)


def graph_like_task_tokens(row: dict[str, Any]) -> list[str]:
    hints = " ".join(row.get("graph_seed_hints", []))
    return unique_ordered(context_target_tokens(f"{row['task']} {hints}"))


def graph_like_file_score(repo: Path, file: FileInfo, tokens: list[str]) -> tuple[int, list[str]]:
    if file.role not in GRAPH_LIKE_SEED_ROLES:
        return 0, []
    path_tokens = re.findall(r"[a-z0-9]+", file.path.lower())
    basename_tokens = re.findall(r"[a-z0-9]+", Path(file.path).stem.lower())
    text = safe_read(repo / file.path, limit=200_000).lower()
    score = 0
    matched: list[str] = []
    for token in tokens:
        path_match = any(token_matches(path_token, token) for path_token in path_tokens)
        basename_match = any(token_matches(path_token, token) for path_token in basename_tokens)
        content_match = bool(text and token in text)
        if path_match:
            score += 8
            matched.append(token)
        if basename_match:
            score += 4
        if content_match:
            score += 1
    if file.role == "test" and matched:
        score += 2
    return score, unique_ordered(matched)


def module_for_context_path(modules: list[ModuleInfo], path: str) -> ModuleInfo | None:
    for module in modules:
        if path in module.owned_files:
            return module
    root = deepest_candidate(path, {module.root for module in modules})
    if root is None:
        return None
    return next((module for module in modules if module.root == root), None)


def add_graph_candidate(
    repo: Path,
    candidates: dict[str, dict[str, Any]],
    file_by_path: dict[str, FileInfo],
    path: str,
    *,
    score: int,
    reason: str,
) -> None:
    if score <= 0 or path not in file_by_path or not (repo / path).is_file():
        return
    file = file_by_path[path]
    entry = candidates.setdefault(
        path,
        {
            "path": path,
            "role": file.role,
            "score": 0,
            "reasons": [],
        },
    )
    entry["score"] += score
    entry["reasons"].append(reason)
    entry["reasons"] = unique_ordered(entry["reasons"])


def graph_import_edges(
    repo: Path,
    files: list[FileInfo],
    package_to_module: dict[str, ModuleInfo],
) -> list[dict[str, str]]:
    edges: list[dict[str, str]] = []
    for file in files:
        if file.role not in {"source", "test"}:
            continue
        for raw in import_strings(repo, file):
            resolved = resolve_import_path(repo, file.path, raw, package_to_module)
            if resolved:
                edges.append({"source": file.path, "raw": raw, "resolved": resolved})
    return edges


def graph_like_context_files(
    repo: Path,
    row: dict[str, Any],
    *,
    max_files: int,
    max_bytes: int,
) -> dict[str, Any]:
    files = inventory(repo)
    file_by_path = {file.path: file for file in files}
    map_data = build_module_map(repo)
    modules = modules_from_map(map_data)
    package_to_module = module_alias_map(modules)
    modules_by_name = {module.name: module for module in modules}
    candidates: dict[str, dict[str, Any]] = {}
    tokens = graph_like_task_tokens(row)

    for hint in row.get("graph_seed_hints", []):
        hinted_path = normalize_context_path(hint)
        if hinted_path in file_by_path:
            add_graph_candidate(
                repo,
                candidates,
                file_by_path,
                hinted_path,
                score=30,
                reason="graph-seed-hint",
            )

    for file in files:
        score, matched = graph_like_file_score(repo, file, tokens)
        if matched:
            add_graph_candidate(
                repo,
                candidates,
                file_by_path,
                file.path,
                score=score,
                reason=f"task-keyword-match:{','.join(matched)}",
            )

    import_edges = graph_import_edges(repo, files, package_to_module)
    role_priority = {"source": 0, "test": 1, "manifest": 2, "docs": 3, "architecture": 4}
    seed_paths = [
        item["path"]
        for item in sorted(
            candidates.values(),
            key=lambda item: (-int(item["score"]), role_priority.get(str(item["role"]), 9), str(item["path"])),
        )
        if item["role"] in {"source", "test"}
    ][:12]

    for seed_path in seed_paths:
        seed_file = file_by_path.get(seed_path)
        if seed_file is None:
            continue
        module = module_for_context_path(modules, seed_path)
        if module:
            for test_path in module.test_files:
                add_graph_candidate(
                    repo,
                    candidates,
                    file_by_path,
                    test_path,
                    score=18,
                    reason=f"nearby-test:{module.name}",
                )
            for manifest_path in module.manifests:
                add_graph_candidate(
                    repo,
                    candidates,
                    file_by_path,
                    manifest_path,
                    score=5,
                    reason=f"nearby-manifest:{module.name}",
                )
            for dep_name in module.dependencies:
                dep = modules_by_name.get(dep_name)
                if not dep:
                    continue
                for dep_path in dep.source_files[:4]:
                    add_graph_candidate(
                        repo,
                        candidates,
                        file_by_path,
                        dep_path,
                        score=10,
                        reason=f"dependency-neighbor:{module.name}->{dep.name}",
                    )

        for edge in import_edges:
            if edge["source"] == seed_path and edge["resolved"] in file_by_path:
                add_graph_candidate(
                    repo,
                    candidates,
                    file_by_path,
                    edge["resolved"],
                    score=14,
                    reason=f"import-neighbor:{seed_path}->{edge['raw']}",
                )
            elif edge["resolved"] == seed_path and edge["source"] in file_by_path:
                add_graph_candidate(
                    repo,
                    candidates,
                    file_by_path,
                    edge["source"],
                    score=10,
                    reason=f"reverse-import-neighbor:{edge['raw']}",
                )

    planned_entries = sorted(
        candidates.values(),
        key=lambda item: (-int(item["score"]), role_priority.get(str(item["role"]), 9), str(item["path"])),
    )
    planned = [
        ContextFile(str(item["path"]), str(item["role"]))
        for item in planned_entries
        if item["role"] in GRAPH_LIKE_SEED_ROLES or item["role"] in GRAPH_LIKE_NEIGHBOR_ROLES
    ]
    included, omitted, selected_bytes = apply_context_limits(repo, planned, max_files=max_files, max_bytes=max_bytes)
    trace = []
    for index, item in enumerate(included, start=1):
        candidate = candidates[item.path]
        trace.append(
            {
                "rank": index,
                "path": item.path,
                "role": item.role,
                "strategy": "graph-like",
                "score": candidate["score"],
                "reasons": candidate["reasons"],
            }
        )
    resolved_module = None
    task_source_entries = [
        item
        for item in planned_entries
        if item["role"] == "source" and any(str(reason).startswith("task-keyword-match:") for reason in item["reasons"])
    ]
    resolved_seed = task_source_entries[0]["path"] if task_source_entries else next((item.path for item in included if item.role == "source"), None)
    if resolved_seed:
        module = module_for_context_path(modules, str(resolved_seed))
        resolved_module = module.name if module else None
    return {
        "files": included,
        "included_files": [item.path for item in included],
        "omitted_files": omitted,
        "selected_bytes": selected_bytes,
        "resolved_module": resolved_module,
        "trace": trace,
    }


def progressive_read_sections(repo: Path, module: ModuleInfo) -> list[str]:
    sections = ["summary"]
    if (repo / module_contract_path(module)).is_file():
        sections.append("contract")
    if (repo / module_agents_path(module)).is_file():
        sections.append("instructions")
    if module.test_files:
        sections.append("tests")
    if module.dependencies:
        sections.append("dependencies")
    return sections


def progressive_mcp_context_files(
    repo: Path,
    row: dict[str, Any],
    *,
    max_files: int,
    max_bytes: int,
) -> dict[str, Any]:
    map_data = build_module_map(repo)
    modules = modules_from_map(map_data)
    plan = plan_context_pack_files_from_modules(repo, row["task"], modules, max_files=max_files, max_bytes=max_bytes)
    module = plan["module"]
    trace: list[dict[str, Any]] = [
        {
            "rank": 1,
            "strategy": "progressive-mcp",
            "tool": "context_discover",
            "operation": "context_discover",
            "module_count": len(modules),
            "selected_module": module.name,
        }
    ]
    for section in progressive_read_sections(repo, module):
        payload = build_context_read_payload(repo, module, modules, section)
        step: dict[str, Any] = {
            "rank": len(trace) + 1,
            "strategy": "progressive-mcp",
            "tool": "context_read",
            "operation": "context_read",
            "target": module.name,
            "section": section,
        }
        if section in {"contract", "instructions"}:
            step["path"] = payload.get("path")
            step["exists"] = payload.get("exists")
        elif section == "tests":
            step["test_count"] = len(payload.get("tests", []))
        elif section == "dependencies":
            step["dependency_count"] = len(payload.get("dependencies", []))
        trace.append(step)
    trace.append(
        {
            "rank": len(trace) + 1,
            "strategy": "progressive-mcp",
            "tool": "context_pack",
            "operation": "context_pack",
            "task": row["task"],
            "target": plan["module"].name,
            "selected_file_count": len(plan["files"]),
            "selected_bytes": plan["selected_bytes"],
        }
    )
    for item in selected_file_trace(plan["files"], "progressive-mcp"):
        item["rank"] = len(trace) + 1
        item["source_tool"] = "context_pack"
        trace.append(item)
    return {
        "files": plan["files"],
        "included_files": plan["included_files"],
        "omitted_files": plan["omitted_files"],
        "selected_bytes": plan["selected_bytes"],
        "resolved_module": plan["module"].name,
        "trace": trace,
    }


def select_context_files_for_strategy(
    repo: Path,
    row: dict[str, Any],
    strategy: str,
    *,
    max_files: int,
    max_bytes: int,
) -> dict[str, Any]:
    if strategy == "naive":
        return naive_context_files(repo, max_files=max_files, max_bytes=max_bytes)
    if strategy == "module":
        plan = plan_context_pack_files(repo, row["task"], max_files=max_files, max_bytes=max_bytes)
    elif strategy == "module-no-contracts":
        plan = plan_context_pack_files(
            repo,
            row["task"],
            max_files=max_files,
            max_bytes=max_bytes,
            include_contracts=False,
        )
    elif strategy == "graph-like":
        return graph_like_context_files(repo, row, max_files=max_files, max_bytes=max_bytes)
    elif strategy == "progressive-mcp":
        return progressive_mcp_context_files(repo, row, max_files=max_files, max_bytes=max_bytes)
    else:
        raise ContextVerifierError(f"unknown context selection strategy: {strategy}")
    return {
        "files": plan["files"],
        "included_files": plan["included_files"],
        "omitted_files": plan["omitted_files"],
        "selected_bytes": plan["selected_bytes"],
        "resolved_module": plan["module"].name,
    }


