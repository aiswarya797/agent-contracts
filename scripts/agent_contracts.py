def desired_refreshes(repo: Path) -> tuple[list[tuple[Path, str, str]], list[tuple[Path, str, str]]]:
    map_data = build_module_map(repo)
    modules = modules_from_map(map_data)
    safe: list[tuple[Path, str, str]] = [(repo / MAP_PATH, "module map JSON", json.dumps(map_data, indent=2) + "\n")]
    contract: list[tuple[Path, str, str]] = []
    for module in modules:
        base = repo if module.root == "." else repo / module.root
        agents_path = base / "AGENTS.md"
        spec_path = base / "SPEC.md"
        desired_agents = generate_module_agents(module) if module.root != "." else generate_root_agents(map_data)
        desired_spec = generate_spec(module)
        if not agents_path.exists() or safe_read(agents_path) != desired_agents:
            safe.append((agents_path, f"{module.name} safe instruction refresh", desired_agents))
        if not spec_path.exists() or safe_read(spec_path) != desired_spec:
            contract.append((spec_path, f"{module.name} contract-changing refresh", desired_spec))
    return safe, contract


def cmd_refresh(args: argparse.Namespace) -> int:
    repo = Path(args.repo).resolve()
    safe, contract = desired_refreshes(repo)
    print("Safe instruction refreshes:")
    for path, description, _ in safe:
        print(f"- {path.relative_to(repo).as_posix()} ({description})")
    print("\nContract-changing updates requiring approval:")
    for path, description, _ in contract:
        print(f"- {path.relative_to(repo).as_posix()} ({description})")
    if args.write_safe:
        result = write_planned_files(safe, yes=args.yes, overwrite_existing=True)
        print("\nSafe writes:")
        print(json.dumps(result, indent=2))
    if args.write_contract:
        result = write_planned_files(contract, yes=args.yes, overwrite_existing=True)
        print("\nContract writes:")
        print(json.dumps(result, indent=2))
    if not args.write_safe and not args.write_contract:
        print("\nNo files written.")
    return 0


def choose_module(modules: list[ModuleInfo], target: str) -> ModuleInfo | None:
    wanted = stable_slug(target)
    for module in modules:
        if module.name == wanted or module.root == target or wanted in {stable_slug(module.root), stable_slug(Path(module.root).name)}:
            return module
    for module in modules:
        haystack = " ".join([module.name, module.root, *module.public_surfaces, *module.capabilities]).lower()
        if target.lower() in haystack:
            return module
    return modules[0] if modules else None


def copy_pack_file(repo: Path, output_root: Path, rel: str, max_bytes: int) -> tuple[bool, str]:
    source = repo / rel
    if not source.exists() or not source.is_file():
        return False, "missing"
    try:
        if source.stat().st_size > max_bytes:
            return False, "too large"
    except OSError:
        return False, "unreadable"
    destination = output_root / "files" / rel
    destination.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(source, destination)
    return True, "included"


def build_context_pack(repo: Path, target: str, output: Path | None, max_files: int, max_bytes: int) -> Path:
    map_data = build_module_map(repo)
    modules = modules_from_map(map_data)
    module = choose_module(modules, target)
    if module is None:
        raise SystemExit("No modules detected.")
    digest = hashlib.sha1(f"{module.name}:{utc_now()}".encode("utf-8")).hexdigest()[:8]
    output_root = output or repo / CONTEXT_PACK_ROOT / f"{module.name}-{digest}"
    output_root.mkdir(parents=True, exist_ok=True)

    include_paths: list[str] = []
    for root_file in ["ARCHITECTURE.md", "AGENTS.md"]:
        if (repo / root_file).exists():
            include_paths.append(root_file)
    for contract_file in ["SPEC.md", "AGENTS.md"]:
        path = contract_file if module.root == "." else f"{module.root}/{contract_file}"
        if (repo / path).exists():
            include_paths.append(path)
    for dep_name in module.dependencies:
        dep = next((item for item in modules if item.name == dep_name), None)
        if dep:
            dep_spec = "SPEC.md" if dep.root == "." else f"{dep.root}/SPEC.md"
            if (repo / dep_spec).exists():
                include_paths.append(dep_spec)
    include_paths.extend(module.source_files)
    include_paths.extend(module.test_files)
    include_paths = list(dict.fromkeys(include_paths))

    included: list[str] = []
    omitted: list[dict[str, str]] = []
    total = 0
    for path in include_paths:
        if len(included) >= max_files:
            omitted.append({"path": path, "reason": "file limit"})
            continue
        size = (repo / path).stat().st_size if (repo / path).exists() else 0
        if total + size > max_bytes:
            omitted.append({"path": path, "reason": "byte limit"})
            continue
        ok, reason = copy_pack_file(repo, output_root, path, max_bytes)
        if ok:
            included.append(path)
            total += size
        else:
            omitted.append({"path": path, "reason": reason})

    manifest = {
        "product": PRODUCT,
        "created_at": utc_now(),
        "target": target,
        "module": module.to_dict(),
        "included_files": included,
        "omitted_files": omitted,
        "limits": {"max_files": max_files, "max_bytes": max_bytes},
    }
    (output_root / "manifest.json").write_text(json.dumps(manifest, indent=2) + "\n", encoding="utf-8")
    readme = [
        f"# Context Pack: {module.name}",
        "",
        GENERATED_BY,
        "",
        f"Target: `{target}`",
        f"Module root: `{module.root}`",
        "",
        "## Verification Commands",
        *([f"- {command}" for command in module.commands] if module.commands else ["- No command detected. Inspect manifests before running anything."]),
        "",
        "## Included Files",
        *(f"- `{path}`" for path in included),
        "",
        "## Omitted Files",
        *([f"- `{item['path']}`: {item['reason']}" for item in omitted] if omitted else ["- None"]),
        "",
    ]
    (output_root / "README.md").write_text("\n".join(readme), encoding="utf-8")
    return output_root


def cmd_context_pack(args: argparse.Namespace) -> int:
    repo = Path(args.repo).resolve()
    output = Path(args.output).resolve() if args.output else None
    pack = build_context_pack(repo, args.target, output, args.max_files, args.max_bytes)
    print(f"Context pack written to {pack}")
    return 0


