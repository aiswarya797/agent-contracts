def classify_selected_context_file(
    path: str,
    *,
    target_files: set[str],
    relevant_tests: set[str],
    required_context: set[str],
    allowed_context: set[str],
    misleading_files: set[str],
    forbidden_paths: set[str] | None = None,
) -> str:
    forbidden_paths = forbidden_paths or set()
    if path in target_files:
        return "target"
    if path in relevant_tests:
        return "relevant_test"
    if path in required_context:
        return "required_context"
    if path in forbidden_paths:
        return "forbidden"
    if path in allowed_context:
        return "allowed_context"
    if path in misleading_files:
        return "misleading"
    return "irrelevant"


def score_selected_context_files(
    repo: Path,
    row: dict[str, Any],
    strategy: str,
    selection: dict[str, Any],
) -> dict[str, Any]:
    selected_files = [normalize_context_path(path) for path in selection["included_files"]]
    selected_set = set(selected_files)
    target_files = set(row["target_files"])
    relevant_tests = set(row["relevant_tests"])
    allowed_context = set(row["allowed_context"])
    required_context = set(row.get("required_context", []))
    misleading_files = set(row["misleading_files"])
    forbidden_paths = set(row.get("forbidden_paths", []))

    file_classifications: list[dict[str, Any]] = []
    bloat_bytes = 0
    allowed_context_count = 0
    required_context_count = 0
    irrelevant_files_read = 0
    forbidden_files_included = 0
    for rank, path in enumerate(selected_files, start=1):
        classification = classify_selected_context_file(
            path,
            target_files=target_files,
            relevant_tests=relevant_tests,
            required_context=required_context,
            allowed_context=allowed_context,
            misleading_files=misleading_files,
            forbidden_paths=forbidden_paths,
        )
        size = verifier_file_size(repo, path)
        if classification == "allowed_context":
            allowed_context_count += 1
        if classification == "required_context":
            required_context_count += 1
        if classification == "forbidden":
            forbidden_files_included += 1
        if classification == "irrelevant":
            irrelevant_files_read += 1
        if classification in {"irrelevant", "misleading", "forbidden"}:
            bloat_bytes += size
        file_classifications.append(
            {
                "rank": rank,
                "path": path,
                "classification": classification,
                "role": verifier_file_role(repo, path),
                "bytes": size,
            }
        )

    target_found = sorted(target_files & selected_set)
    relevant_tests_found_paths = sorted(relevant_tests & selected_set)
    required_context_found_paths = sorted(required_context & selected_set)
    misleading_included = sorted(misleading_files & selected_set)
    forbidden_included = sorted(forbidden_paths & selected_set)
    target_file_recall = len(target_found) / len(target_files) if target_files else 1.0
    relevant_test_recall = len(relevant_tests_found_paths) / len(relevant_tests) if relevant_tests else 1.0
    required_context_recall = len(required_context_found_paths) / len(required_context) if required_context else 1.0
    first_target_file_rank = next((index for index, path in enumerate(selected_files, start=1) if path in target_files), len(selected_files) + 1)
    selected_bytes = int(selection["selected_bytes"])
    resolved_module = selection.get("resolved_module")
    expected_module_match = None if resolved_module is None else resolved_module == row["expected_module"]
    expected_module_passed = strategy == "naive" or expected_module_match is True
    passed = bool(
        target_file_recall >= 1.0
        and relevant_test_recall >= 1.0
        and required_context_recall >= 1.0
        and not misleading_included
        and not forbidden_included
        and expected_module_passed
    )
    trace = selection.get("trace") or selected_file_trace(selection["files"], strategy)
    return {
        "strategy": strategy,
        "resolved_module": resolved_module,
        "expected_module": row["expected_module"],
        "expected_module_match": expected_module_match,
        "selected_files": selected_files,
        "selected_file_count": len(selected_files),
        "selected_bytes": selected_bytes,
        "target_file_recall": round(target_file_recall, 6),
        "target_files_found": target_found,
        "target_files_missing": sorted(target_files - selected_set),
        "relevant_tests_found": round(relevant_test_recall, 6),
        "relevant_test_recall": round(relevant_test_recall, 6),
        "relevant_test_files_found": relevant_tests_found_paths,
        "relevant_test_files_missing": sorted(relevant_tests - selected_set),
        "allowed_context_count": allowed_context_count,
        "required_context_found": round(required_context_recall, 6),
        "required_context_recall": round(required_context_recall, 6),
        "required_context_files_found": required_context_found_paths,
        "required_context_files_missing": sorted(required_context - selected_set),
        "required_context_count": required_context_count,
        "irrelevant_files_read": irrelevant_files_read,
        "misleading_files_included": len(misleading_included),
        "misleading_file_paths": misleading_included,
        "forbidden_files_included": forbidden_files_included,
        "forbidden_file_paths": forbidden_included,
        "context_bloat": round(bloat_bytes / max(selected_bytes, 1), 6),
        "first_target_file_rank": first_target_file_rank,
        "passed": passed,
        "file_classifications": file_classifications,
        "trace": trace,
        "omitted_files": selection["omitted_files"],
    }


def path_rank(paths: list[str], path: str, default: int | None = None) -> int:
    try:
        return paths.index(path) + 1
    except ValueError:
        return default if default is not None else len(paths) + 1


def plausible_misleading_edit_path(
    repo: Path,
    row: dict[str, Any],
    score: dict[str, Any],
    *,
    before_target_only: bool,
) -> str | None:
    selected_files = list(score["selected_files"])
    first_target_rank = int(score["first_target_file_rank"])
    misleading_read = list(score["misleading_file_paths"])
    if before_target_only:
        candidates = [
            path
            for path in misleading_read
            if path_rank(selected_files, path) < first_target_rank
        ]
    else:
        candidates = misleading_read or list(row["misleading_files"])
    if not candidates:
        return None

    manifest_order = {path: index for index, path in enumerate(row["misleading_files"], start=1)}

    def candidate_key(path: str) -> tuple[int, int, int, int, str]:
        role = verifier_file_role(repo, path)
        root_priority = 0 if path.startswith("src/") or "/src/" in path else 1
        role_priority = {"source": 0, "test": 1, "docs": 2, "architecture": 3, "manifest": 4}.get(role, 5)
        rank = path_rank(selected_files, path, len(selected_files) + manifest_order.get(path, 999))
        return (root_priority, role_priority, rank, manifest_order.get(path, 999), path)

    return sorted(candidates, key=candidate_key)[0]


def append_trial_step(trace: list[dict[str, Any]], operation: str, **fields: Any) -> None:
    step: dict[str, Any] = {
        "rank": len(trace) + 1,
        "phase": "trial",
        "operation": operation,
    }
    step.update(fields)
    trace.append(step)


def simulate_context_trial(
    repo: Path,
    row: dict[str, Any],
    strategy: str,
    selection: dict[str, Any],
) -> dict[str, Any]:
    score = score_selected_context_files(repo, row, strategy, selection)
    read_paths = list(score["selected_files"])
    read_set = set(read_paths)
    read_bytes = int(score["selected_bytes"])
    trace = [dict(item) for item in score["trace"]]
    append_trial_step(
        trace,
        "read_selected_context",
        strategy=strategy,
        file_count=len(read_paths),
        selected_bytes=score["selected_bytes"],
    )

    failure_reasons: list[str] = []
    if score["target_files_missing"]:
        failure_reasons.append("missing-target-files")
    if score["relevant_test_files_missing"]:
        failure_reasons.append("missing-relevant-tests")
    if score["required_context_files_missing"]:
        failure_reasons.append("missing-required-context")
    if score["forbidden_file_paths"]:
        failure_reasons.append("forbidden-path-read")
    if strategy in MODULE_LIKE_CONTEXT_STRATEGIES and score["expected_module_match"] is not True:
        failure_reasons.append("expected-module-mismatch")

    selected_misleading_before_target = [
        path
        for path in score["misleading_file_paths"]
        if path_rank(score["selected_files"], path) < int(score["first_target_file_rank"])
    ]
    edit_misleading = bool(selected_misleading_before_target or score["required_context_files_missing"])
    files_edited: list[str] = []
    misleading_files_edited: list[str] = []

    if edit_misleading:
        edit_path = plausible_misleading_edit_path(
            repo,
            row,
            score,
            before_target_only=bool(selected_misleading_before_target),
        )
        if edit_path:
            if edit_path not in read_set:
                read_paths.append(edit_path)
                read_set.add(edit_path)
                read_bytes += verifier_file_size(repo, edit_path)
                append_trial_step(
                    trace,
                    "read_fallback_misleading_file",
                    path=edit_path,
                    reason="required-context-missing",
                )
            files_edited.append(edit_path)
            misleading_files_edited.append(edit_path)
            failure_reasons.append("misleading-file-edited")
            append_trial_step(
                trace,
                "edit_misleading_file",
                path=edit_path,
                reason="misleading-before-target" if selected_misleading_before_target else "required-context-missing",
            )
    elif not score["target_files_missing"]:
        files_edited.extend(row["target_files"])
        for path in row["target_files"]:
            append_trial_step(trace, "edit_target_file", path=path, reason="target-context-complete")

    failure_reasons = unique_ordered(failure_reasons)
    trial_success = not failure_reasons and set(row["target_files"]).issubset(set(files_edited))
    if trial_success:
        append_trial_step(trace, "trial_success", reason="targets-tests-context-satisfied")
    else:
        append_trial_step(trace, "trial_failure", reasons=failure_reasons or ["target-files-not-edited"])

    target_files_read = sorted(set(row["target_files"]) & read_set)
    target_files_edited = sorted(set(row["target_files"]) & set(files_edited))
    relevant_tests_read = sorted(set(row["relevant_tests"]) & read_set)
    required_context_read = sorted(set(row.get("required_context", [])) & read_set)
    misleading_files_read = sorted(set(row["misleading_files"]) & read_set)

    return {
        "strategy": strategy,
        "resolved_module": score["resolved_module"],
        "expected_module": score["expected_module"],
        "expected_module_match": score["expected_module_match"],
        "files_read": len(read_paths),
        "files_read_paths": read_paths,
        "files_edited": len(files_edited),
        "files_edited_paths": files_edited,
        "target_files_read": len(target_files_read),
        "target_files_read_paths": target_files_read,
        "target_files_missing": score["target_files_missing"],
        "target_files_edited": len(target_files_edited),
        "target_files_edited_paths": target_files_edited,
        "relevant_tests_read": len(relevant_tests_read),
        "relevant_tests_read_paths": relevant_tests_read,
        "relevant_tests_missing": score["relevant_test_files_missing"],
        "misleading_files_read": len(misleading_files_read),
        "misleading_files_read_paths": misleading_files_read,
        "misleading_files_edited": len(misleading_files_edited),
        "misleading_files_edited_paths": misleading_files_edited,
        "required_context_read": len(required_context_read),
        "required_context_read_paths": required_context_read,
        "required_context_missing": score["required_context_files_missing"],
        "required_context_recall": score["required_context_recall"],
        "selected_bytes": score["selected_bytes"],
        "read_bytes": read_bytes,
        "first_target_file_rank": score["first_target_file_rank"],
        "trial_success": trial_success,
        "failure_reason": None if trial_success else ", ".join(failure_reasons or ["target-files-not-edited"]),
        "selection_score": {
            "passed": score["passed"],
            "target_file_recall": score["target_file_recall"],
            "relevant_test_recall": score["relevant_test_recall"],
            "required_context_recall": score["required_context_recall"],
            "misleading_files_included": score["misleading_files_included"],
            "irrelevant_files_read": score["irrelevant_files_read"],
        },
        "file_classifications": score["file_classifications"],
        "trace": trace,
        "omitted_files": score["omitted_files"],
    }


def parse_strategy_names(value: str | None, allowed: tuple[str, ...]) -> tuple[str, ...]:
    if value is None or not value.strip():
        return allowed
    requested = tuple(unique_ordered([item.strip() for item in value.split(",") if item.strip()]))
    unknown = sorted(set(requested) - set(allowed))
    if unknown:
        raise ContextVerifierError(f"unknown strategy name(s): {', '.join(unknown)}")
    if not requested:
        raise ContextVerifierError("at least one strategy must be selected")
    return requested


def selected_context_entries(repo: Path, selection: dict[str, Any]) -> list[dict[str, Any]]:
    entries: list[dict[str, Any]] = []
    roles = {item.path: item.role for item in selection.get("files", [])}
    for path in selection["included_files"]:
        absolute = repo / path
        content = safe_read(absolute)
        try:
            size = absolute.stat().st_size
        except OSError:
            size = 0
        entries.append(
            {
                "path": path,
                "role": roles.get(path, verifier_file_role(repo, path)),
                "bytes": size,
                "content": content,
            }
        )
    return entries


