def dependency_context_entries(repo: Path, module: ModuleInfo, modules: list[ModuleInfo]) -> list[dict[str, Any]]:
    modules_by_name = {item.name: item for item in modules}
    entries: list[dict[str, Any]] = []
    for dep_name in module.dependencies:
        dep = modules_by_name.get(dep_name)
        contract_path = module_contract_path(dep) if dep else ""
        entries.append(
            {
                "name": dep_name,
                "root": dep.root if dep else "",
                "kind": dep.kind if dep else "",
                "contract": {
                    "path": contract_path,
                    "exists": bool(contract_path and (repo / contract_path).is_file()),
                },
                "imports": [
                    dataclasses.asdict(item)
                    for item in module.imports
                    if item.target_module == dep_name
                ],
            }
        )
    return entries


def module_related_paths(module: ModuleInfo) -> set[str]:
    paths = set(module.owned_files)
    paths.add(module_contract_path(module))
    paths.add(module_agents_path(module))
    return paths


def finding_applies_to_module(finding: Finding, module: ModuleInfo) -> bool:
    related = module_related_paths(module)
    affected = {path for path in finding.affected_files if path}
    if affected & related:
        return True
    if any(prefix_match(path, module.root) for path in affected):
        return True
    evidence = finding.evidence.lower()
    return f"`{module.name.lower()}`" in evidence or f" {module.name.lower()} " in f" {evidence} "


def module_drift_payload(repo: Path, module: ModuleInfo) -> dict[str, Any]:
    findings = [finding for finding in check_drift(repo) if finding_applies_to_module(finding, module)]
    return {
        "findings": [finding.to_dict() for finding in findings],
        "summary": {"finding_count": len(findings)},
    }


def build_context_read_payload(
    repo: Path,
    module: ModuleInfo,
    modules: list[ModuleInfo],
    section: str,
) -> dict[str, Any]:
    payload: dict[str, Any] = {"product": PRODUCT, "module": module.name, "section": section}
    if section == "summary":
        payload.update(context_catalog_entry(repo, module, modules))
        payload["public_surfaces"] = module.public_surfaces
    elif section == "contract":
        payload.update(context_file_payload(repo, module_contract_path(module)))
        payload.update(
            {
                "capabilities": module.capabilities,
                "public_surfaces": module.public_surfaces,
                "dependencies": module.dependencies,
                "commands": module.commands,
            }
        )
    elif section == "instructions":
        payload.update(context_file_payload(repo, module_agents_path(module)))
        payload.update(
            {
                "commands": module.commands,
                "tests": module.test_files,
                "dependencies": module.dependencies,
                "boundary_notes": module.boundary_notes,
            }
        )
    elif section == "dependencies":
        payload["dependencies"] = dependency_context_entries(repo, module, modules)
    elif section == "tests":
        payload["tests"] = module.test_files
        payload["commands"] = module.commands
    elif section == "source-list":
        payload["source_files"] = module.source_files
        payload["source_count"] = len(module.source_files)
    elif section == "drift":
        payload.update(module_drift_payload(repo, module))
    return payload


