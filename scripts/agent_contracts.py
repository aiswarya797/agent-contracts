def format_optional_float(value: Any, precision: int = 2) -> str:
    if value is None:
        return "n/a"
    if isinstance(value, (int, float)):
        return f"{float(value):.{precision}f}"
    return str(value)


def signed_optional_float(value: Any, precision: int = 2) -> str:
    if value is None:
        return "n/a"
    if isinstance(value, (int, float)):
        return f"{float(value):+.{precision}f}"
    return str(value)


