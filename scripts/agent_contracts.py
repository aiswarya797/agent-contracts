def add_doctor_check(checks: list[dict[str, str]], name: str, status: str, detail: str) -> None:
    checks.append({"name": name, "status": status, "detail": detail})


def run_git(repo: Path, *args: str) -> subprocess.CompletedProcess[str] | None:
    if shutil.which("git") is None:
        return None
    return subprocess.run(
        ["git", *args],
        cwd=repo,
        text=True,
        capture_output=True,
        check=False,
    )


def doctor_report(repo: Path) -> dict[str, Any]:
    checks: list[dict[str, str]] = []
    resolved_repo = repo.resolve()
    plugin_root = Path(__file__).resolve().parents[1]

    if resolved_repo.exists() and resolved_repo.is_dir():
        add_doctor_check(checks, "repository path", "ok", f"`{resolved_repo}` exists and is a directory.")
    else:
        add_doctor_check(checks, "repository path", "blocker", f"`{resolved_repo}` is not a readable directory.")
        return {"product": PRODUCT, "repo_root": resolved_repo.as_posix(), "checks": checks, "exit_code": 2}

    python_version = f"{sys.version_info.major}.{sys.version_info.minor}.{sys.version_info.micro}"
    if sys.version_info >= (3, 10):
        add_doctor_check(checks, "python version", "ok", f"Python {python_version} is supported.")
    else:
        add_doctor_check(checks, "python version", "blocker", f"Python {python_version} is too old; Python 3.10+ is required.")

    missing_plugin_files = [relative for relative in plugin_required_paths() if not (plugin_root / relative).exists()]
    if missing_plugin_files:
        add_doctor_check(
            checks,
            "plugin layout",
            "blocker",
            "Missing plugin files: " + ", ".join(missing_plugin_files[:8]) + (" ..." if len(missing_plugin_files) > 8 else ""),
        )
    else:
        add_doctor_check(checks, "plugin layout", "ok", f"Required plugin files exist under `{plugin_root}`.")

    git_root = find_git_root(resolved_repo)
    if shutil.which("git") is None:
        add_doctor_check(checks, "git availability", "warning", "Git was not found on PATH; analysis still works, but repo status checks are limited.")
    elif git_root is None:
        add_doctor_check(checks, "git repository", "warning", "No `.git` directory found at or above the target path.")
    else:
        add_doctor_check(checks, "git repository", "ok", f"Git repository root detected at `{git_root}`.")
        status = run_git(resolved_repo, "status", "--short")
        if status is None:
            add_doctor_check(checks, "git status", "warning", "Git status could not be checked.")
        elif status.returncode == 0:
            count = len([line for line in status.stdout.splitlines() if line.strip()])
            detail = "Working tree is clean." if count == 0 else f"Working tree has {count} changed or untracked path(s)."
            add_doctor_check(checks, "git status", "ok", detail)
        else:
            add_doctor_check(checks, "git status", "warning", status.stderr.strip() or "Git status failed.")

    if os.access(resolved_repo, os.R_OK):
        add_doctor_check(checks, "read permission", "ok", "Repository path is readable.")
    else:
        add_doctor_check(checks, "read permission", "blocker", "Repository path is not readable.")

    if os.access(resolved_repo, os.W_OK):
        add_doctor_check(checks, "write permission", "ok", "Repository path is writable for approved generated files.")
    else:
        add_doctor_check(checks, "write permission", "warning", "Repository path is not writable; read-only commands can still run.")

    gitignore_path = resolved_repo / ".gitignore"
    if gitignore_path.exists():
        gitignore_text = safe_read(gitignore_path)
        expected_ignores = [".agent-contracts/cache/", ".agent-contracts/context-packs/"]
        missing_ignores = [item for item in expected_ignores if item not in gitignore_text]
        if missing_ignores:
            add_doctor_check(checks, "ignore rules", "warning", "Consider ignoring transient outputs: " + ", ".join(missing_ignores))
        else:
            add_doctor_check(checks, "ignore rules", "ok", "Transient agent-contracts outputs are ignored.")
    else:
        add_doctor_check(checks, "ignore rules", "warning", "No `.gitignore` found; generated caches and context packs may be accidentally committed.")

    sample = inventory(resolved_repo)
    if sample:
        add_doctor_check(checks, "inventory sample", "ok", f"Read-only inventory found {len(sample)} text file(s).")
    else:
        add_doctor_check(checks, "inventory sample", "warning", "No supported text files were found in the target repository.")

    blockers = sum(1 for check in checks if check["status"] == "blocker")
    warnings = sum(1 for check in checks if check["status"] == "warning")
    exit_code = 2 if blockers else 1 if warnings else 0
    return {
        "product": PRODUCT,
        "repo_root": resolved_repo.as_posix(),
        "plugin_root": plugin_root.as_posix(),
        "local_only": True,
        "checks": checks,
        "summary": {"blockers": blockers, "warnings": warnings, "ok": sum(1 for check in checks if check["status"] == "ok")},
        "exit_code": exit_code,
    }


