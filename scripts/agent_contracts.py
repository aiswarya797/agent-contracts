def compact_list(items: list[str], empty: str = "none detected") -> str:
    return ", ".join(items) if items else empty


def context_target_tokens(target: str) -> list[str]:
    return [
        token
        for token in re.findall(r"[a-z0-9]+", target.lower())
        if len(token) > 1 and token not in CONTEXT_TARGET_STOP_WORDS
    ]


def module_target_score(module: ModuleInfo, target: str) -> int:
    normalized = target.strip().lower()
    wanted = stable_slug(target)
    root_leaf = Path(module.root).name if module.root != "." else module.name
    exact_aliases = {
        module.name.lower(),
        module.root.lower(),
        stable_slug(module.root),
        stable_slug(root_leaf),
    }
    exact_aliases.update(package.lower() for package in module.package_names)
    if normalized in exact_aliases or wanted in exact_aliases:
        return 1000

    identity = " ".join([module.name, module.root, root_leaf, *module.package_names]).lower()
    evidence = " ".join(
        [
            *module.public_surfaces,
            *module.capabilities,
            *module.source_files,
            *module.test_files,
            *module.commands,
            *module.docs,
        ]
    ).lower()
    score = 0
    if normalized and normalized in f"{identity} {evidence}":
        score += 25
    for token in context_target_tokens(target):
        if token in identity:
            score += 10
        if token in evidence:
            score += 4
    return score


def resolve_context_module(modules: list[ModuleInfo], target: str, *, allow_fallback: bool) -> ModuleInfo | None:
    if not modules:
        return None
    scored = [
        (module_target_score(module, target), index, module)
        for index, module in enumerate(modules)
    ]
    matches = [item for item in scored if item[0] > 0]
    if matches:
        matches.sort(key=lambda item: (-item[0], item[1]))
        return matches[0][2]
    return modules[0] if allow_fallback else None


def module_contract_path(module: ModuleInfo) -> str:
    return "SPEC.md" if module.root == "." else f"{module.root}/SPEC.md"


def module_agents_path(module: ModuleInfo) -> str:
    return "AGENTS.md" if module.root == "." else f"{module.root}/AGENTS.md"


def add_context_file(repo: Path, files: list[ContextFile], seen: set[str], path: str, role: str) -> None:
    if path in seen or not (repo / path).is_file():
        return
    files.append(ContextFile(path, role))
    seen.add(path)


def context_file_plan(
    repo: Path,
    module: ModuleInfo,
    modules: list[ModuleInfo],
    *,
    include_contracts: bool = True,
) -> list[ContextFile]:
    files: list[ContextFile] = []
    seen: set[str] = set()
    if include_contracts:
        for path, role in [
            ("ARCHITECTURE.md", "root-contract"),
            ("AGENTS.md", "root-instructions"),
            (module_contract_path(module), "module-contract"),
            (module_agents_path(module), "module-instructions"),
        ]:
            add_context_file(repo, files, seen, path, role)

        modules_by_name = {item.name: item for item in modules}
        for dep_name in module.dependencies:
            dep = modules_by_name.get(dep_name)
            if dep:
                add_context_file(repo, files, seen, module_contract_path(dep), "dependency-contract")

    for path in module.source_files:
        add_context_file(repo, files, seen, path, "source")
    for path in module.test_files:
        add_context_file(repo, files, seen, path, "test")
    return files


def context_size_summary(repo: Path, files: list[ContextFile]) -> dict[str, int]:
    total = 0
    count = 0
    for item in files:
        path = repo / item.path
        try:
            if path.is_file():
                total += path.stat().st_size
                count += 1
        except OSError:
            continue
    return {"files": count, "bytes": total}


def apply_context_limits(
    repo: Path,
    files: list[ContextFile],
    *,
    max_files: int,
    max_bytes: int,
) -> tuple[list[ContextFile], list[dict[str, str]], int]:
    included: list[ContextFile] = []
    omitted: list[dict[str, str]] = []
    total = 0
    for item in files:
        path = repo / item.path
        if len(included) >= max_files:
            omitted.append({"path": item.path, "reason": "file limit"})
            continue
        try:
            size = path.stat().st_size
        except OSError:
            omitted.append({"path": item.path, "reason": "unreadable"})
            continue
        if total + size > max_bytes:
            omitted.append({"path": item.path, "reason": "byte limit"})
            continue
        included.append(item)
        total += size
    return included, omitted, total


def plan_context_pack_files(
    repo: Path,
    target: str,
    *,
    max_files: int,
    max_bytes: int,
    include_contracts: bool = True,
    allow_fallback: bool = True,
) -> dict[str, Any]:
    map_data = build_module_map(repo)
    modules = modules_from_map(map_data)
    return plan_context_pack_files_from_modules(
        repo,
        target,
        modules,
        max_files=max_files,
        max_bytes=max_bytes,
        include_contracts=include_contracts,
        allow_fallback=allow_fallback,
    )


def plan_context_pack_files_from_modules(
    repo: Path,
    target: str,
    modules: list[ModuleInfo],
    *,
    max_files: int,
    max_bytes: int,
    include_contracts: bool = True,
    allow_fallback: bool = True,
) -> dict[str, Any]:
    module = resolve_context_module(modules, target, allow_fallback=allow_fallback)
    if module is None:
        raise ValueError(f"No module matched target: {target}")
    planned = context_file_plan(repo, module, modules, include_contracts=include_contracts)
    included, omitted, selected_bytes = apply_context_limits(repo, planned, max_files=max_files, max_bytes=max_bytes)
    return {
        "module": module,
        "modules": modules,
        "files": included,
        "included_files": [item.path for item in included],
        "omitted_files": omitted,
        "selected_bytes": selected_bytes,
    }


