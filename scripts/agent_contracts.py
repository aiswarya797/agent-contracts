def prompt_yes_no(question: str) -> bool:
    try:
        answer = input(f"{question} [y/N] ").strip().lower()
    except EOFError:
        return False
    return answer in {"y", "yes"}


def write_planned_files(
    planned: list[tuple[Path, str, str]],
    *,
    yes: bool,
    overwrite_existing: bool,
) -> dict[str, list[str]]:
    existing = [path for path, _, _ in planned if path.exists()]
    if not yes and not prompt_yes_no(f"Write {len(planned)} generated files?"):
        return {"written": [], "skipped_existing": [], "skipped_other": [path.as_posix() for path, _, _ in planned]}
    if existing and not yes and not prompt_yes_no(f"Overwrite {len(existing)} existing files?"):
        return {
            "written": [],
            "skipped_existing": [path.as_posix() for path in existing],
            "skipped_other": [],
        }
    result = {"written": [], "skipped_existing": [], "skipped_other": []}
    for path, _, contents in planned:
        if path.exists() and not overwrite_existing:
            result["skipped_existing"].append(path.as_posix())
            continue
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(contents, encoding="utf-8")
        result["written"].append(path.as_posix())
    return result


def find_git_root(repo: Path) -> Path | None:
    current = repo.resolve()
    for candidate in [current, *current.parents]:
        if (candidate / ".git").exists():
            return candidate
    return None


