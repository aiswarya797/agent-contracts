def run_one_agent_context_trial(
    row: dict[str, Any],
    fixture_repo: Path,
    *,
    strategy: str,
    mode: str,
    run_index: int,
    max_files: int,
    max_bytes: int,
    timeout_seconds: int,
    agent_command: str | None,
    temp_parent: Path,
) -> dict[str, Any]:
    temp_repo = copy_fixture_for_agent_trial(fixture_repo, temp_parent, row, strategy, run_index)
    initialize_trial_git_repo(temp_repo)
    selection = select_context_files_for_strategy(
        temp_repo,
        row,
        strategy,
        max_files=max_files,
        max_bytes=max_bytes,
    )
    agent_input = build_agent_trial_input(
        temp_repo,
        row,
        strategy,
        selection,
        run_index=run_index,
        max_files=max_files,
        max_bytes=max_bytes,
    )
    if mode == "mock":
        agent_result = run_mock_agent(temp_repo, row, strategy, selection)
    else:
        if not agent_command:
            raise ContextVerifierError("--agent-command is required when --mode subprocess")
        agent_result = run_subprocess_agent(
            temp_repo,
            agent_command,
            agent_input,
            timeout_seconds=timeout_seconds,
        )
    diff = git_diff_text(temp_repo)
    scored = score_agent_trial_run(
        temp_repo,
        row,
        strategy,
        selection,
        agent_result,
        mode=mode,
        run_index=run_index,
        git_diff=diff,
    )
    scored["temp_repo_path"] = temp_repo.as_posix()
    scored["selected_context"] = {
        "included_files": selection.get("included_files", []),
        "selected_bytes": selection.get("selected_bytes", 0),
        "resolved_module": selection.get("resolved_module"),
        "omitted_files": selection.get("omitted_files", []),
    }
    return scored


